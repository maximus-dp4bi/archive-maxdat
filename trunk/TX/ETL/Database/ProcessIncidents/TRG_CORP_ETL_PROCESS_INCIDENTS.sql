CREATE OR REPLACE TRIGGER "TRG_R_CORP_ETL_PROCES_INCDENTS" BEFORE
  INSERT OR
  UPDATE ON corp_etl_process_incidents FOR EACH ROW 
BEGIN 
  if INSERTING then 
    IF :new.cepi_Id IS NULL THEN
      SELECT Seq_cepi_Id.Nextval INTO :NEW.cepi_Id FROM Dual;
    END IF;
    IF :NEW.STG_EXTRACT_DATE IS NULL THEN
      :NEW.STG_EXTRACT_DATE  := SYSDATE;
    END IF;
  END IF;
  :NEW.STG_LAST_UPDATE_DATE := SYSDATE;
end;
/
ALTER TRIGGER "TRG_R_CORP_ETL_PROCES_INCDENTS" ENABLE;