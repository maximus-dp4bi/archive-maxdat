CREATE OR REPLACE VIEW EMRS_D_PA_CHC_INDICATOR_SV
AS
SELECT ci.value AS CHC_INDICATOR_CD
      ,ci.report_label AS CHC_INDICATOR
FROM ats.enum_chc_indicator_type ci
WHERE effective_end_date IS NULL
UNION ALL
SELECT 'UD' AS CHC_INDICATOR_CD
      , 'Undefined' AS CHC_INDICATOR
FROM DUAL;

GRANT SELECT ON EMRS_D_PA_CHC_INDICATOR_SV TO MAXDAT_REPORTS;