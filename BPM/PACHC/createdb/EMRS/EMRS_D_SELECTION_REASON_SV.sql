CREATE OR REPLACE VIEW EMRS_D_SELECTION_REASON_SV
AS
  SELECT e.value AS SELECTION_REASON_CODE ,
    e.description AS SELECTION_REASON 
  FROM enum_choice_reason e
  UNION
  SELECT '0' AS SELECTION_REASON_CODE ,
    'Not Defined' AS SELECTION_REASON 
  FROM DUAL; 
  
  GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_SELECTION_REASON_SV TO MAXDAT_REPORTS; 