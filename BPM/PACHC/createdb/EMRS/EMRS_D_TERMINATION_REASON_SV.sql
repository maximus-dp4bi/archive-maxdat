CREATE OR REPLACE VIEW EMRS_D_TERMINATION_REASON_SV
AS
 SELECT edr.value AS REASON_CODE,
    edr.description AS REASON 
  FROM enum_disenrollment_reason edr
  UNION
   SELECT '0' AS REASON_CODE ,
    'Not Defined' AS REASON 
  FROM DUAL  ; 
  
  GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_TERMINATION_REASON_SV TO MAXDAT_REPORTS;  