CREATE OR REPLACE VIEW EMRS_D_MI_TYPE_SV
AS
  SELECT m.value AS MISSING_INFO_TYPE_CD ,
    m.description AS MISSING_INFO_TYPE  
  FROM ats.ENUM_MISSING_INFO_TYPE m
  UNION
  SELECT '0' AS MISSING_INFO_TYPE_CD ,
    'Not Defined' AS MISSING_INFO_TYPE  
  FROM DUAL; 
  
  GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_MI_TYPE_SV TO MAXDAT_REPORTS;