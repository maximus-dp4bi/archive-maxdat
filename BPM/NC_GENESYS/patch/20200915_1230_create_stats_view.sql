
CREATE OR REPLACE VIEW GENESYS_ETL_JOB_STATISTICS_SV
AS SELECT
	JOB_ID,
	JOB_NAME, 
	JOB_STATUS_CD, 
	FILE_NAME, 
	RECORD_COUNT, 
    PROCESSED_COUNT, 
	ERROR_COUNT, 
	WARNING_COUNT, 
    RECORD_INSERTED_COUNT, 
	RECORD_UPDATED_COUNT, 
	JOB_START_DATE, 
	JOB_END_DATE, 
	PARENT_JOB_ID
FROM GENESYS_ETL_JOB_STATISTICS 
WITH READ ONLY;

