
CREATE TABLE D_SUBPROGRAM
  (SUBPROGRAM_CODE VARCHAR2(32), 	
   SUBPROGRAM_NAME VARCHAR2(240), 
   REPORT_LABEL  VARCHAR2(100),
   PROGRAM_CODE	varchar2(32),
   START_DATE DATE,
   END_DATE DATE,
   PLAN_SERVICE_TYPE VARCHAR2(20)
   ) TABLESPACE &tablespace_name ;

ALTER TABLE "D_SUBPROGRAM" ADD CONSTRAINT "SUBPROG_SUBPROG_COMB_UK" UNIQUE ("SUBPROGRAM_CODE", "PROGRAM_CODE") USING INDEX TABLESPACE &tablespace_name  ENABLE; 
ALTER TABLE "D_SUBPROGRAM" ADD CONSTRAINT "SUBPROG_PK" PRIMARY KEY ("SUBPROGRAM_CODE") USING INDEX TABLESPACE &tablespace_name  ENABLE;
  
GRANT SELECT ON "D_SUBPROGRAM" TO &role_name;

CREATE OR REPLACE VIEW D_SUBPROGRAM_SV
AS
SELECT SUBPROGRAM_CODE subprogram_code
       ,SUBPROGRAM_name subprogram_name
       ,PROGRAM_CODE
       , REPORT_LABEL
       ,start_date
       ,end_date
       ,plan_service_type
FROM d_SUBPROGRAM;

GRANT SELECT ON "D_SUBPROGRAM_SV" TO &role_name;

