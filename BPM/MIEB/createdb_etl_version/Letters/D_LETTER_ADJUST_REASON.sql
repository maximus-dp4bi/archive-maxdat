
CREATE TABLE D_LETTER_ADJUST_REASON(
LETTER_ADJUST_REASON_CODE	VARCHAR2(32)	,
NAME  VARCHAR2(100),
DESCRIPTION         VARCHAR2(240),
REPORT_LABEL        VARCHAR2(100),
EFFECTIVE_FROM_DATE	DATE	,
EFFECTIVE_THRU_DATE	DATE) 	TABLESPACE &tablespace_name;

CREATE OR REPLACE VIEW D_LETTER_ADJUST_REASON_SV AS 
SELECT LETTER_ADJUST_REASON_CODE
, NAME
, DESCRIPTION
, REPORT_LABEL
, EFFECTIVE_FROM_DATE
, EFFECTIVE_THRU_DATE
FROM D_LETTER_ADJUST_REASON
;

GRANT SELECT ON D_LETTER_ADJUST_REASON_SV TO &role_name;

