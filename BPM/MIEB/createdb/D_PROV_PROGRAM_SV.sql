CREATE OR REPLACE VIEW D_PROV_PROGRAM_SV AS
SELECT 'Mainstream' PROV_PROGRAM_CODE, 'Mainstream' PROV_PROGRAM_NAME FROM DUAL
UNION
SELECT 'ICO' PROV_PROGRAM_CODE, 'ICO' PROV_PROGRAM_NAME FROM DUAL
UNION
SELECT 'Dental' PROV_PROGRAM_CODE, 'Dental' PROV_PROGRAM_NAME FROM DUAL
;

GRANT SELECT ON MAXDAT_SUPPORT.d_prov_program_sv TO MAXDAT_REPORTS ;
