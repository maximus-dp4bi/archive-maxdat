CREATE OR REPLACE VIEW D_PI_REPORTER_RELAT_BY_SV
AS
  SELECT rr.value REPORTER_RELATIONSHIP_CODE,
    rr.report_label REPORTER_RELATIONSHIP
  FROM ats.enum_relationship rr
  UNION ALL
  SELECT '0' REPORTER_RELATIONSHIP_CODE,
    NULL REPORTER_RELATIONSHIP
  FROM DUAL;
  
  GRANT SELECT ON MAXDAT_SUPPORT.D_PI_REPORTER_RELAT_BY_SV TO MAXDAT_REPORTS ; 