CREATE OR REPLACE VIEW D_PI_RESOLUTION_TYPE_SV
AS
  SELECT value RESOLUTION_TYPE_CODE ,
    report_label RESOLUTION_TYPE
  FROM ats.enum_resolution_type
  UNION ALL
  SELECT '0' AS RESOLUTION_TYPE_CODE ,
    NULL AS  RESOLUTION_TYPE
  FROM DUAL;
  
  GRANT SELECT ON MAXDAT_SUPPORT.D_PI_RESOLUTION_TYPE_SV TO MAXDAT_REPORTS ; 
  
  