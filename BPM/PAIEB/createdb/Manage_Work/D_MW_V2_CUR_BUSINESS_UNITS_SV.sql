CREATE OR REPLACE VIEW D_MW_V2_CUR_BUSINESS_UNITS_SV
AS
  SELECT g.group_id AS BUSINESS_UNIT_ID ,
    g.group_name AS BUSINESS_UNIT_NAME,
    g.description AS BUSINESS_UNIT_DESCRIPTION
  FROM GROUPS g
  WHERE g.type_cd = 'BIZUNIT'
  UNION 
  SELECT 
    0 AS BUSINESS_UNIT_ID ,
    NULL AS BUSINESS_UNIT_NAME,
    NULL AS BUSINESS_UNIT_DESCRIPTION
  FROM DUAL;
  
  GRANT SELECT ON MAXDAT_SUPPORT.D_MW_V2_CUR_BUSINESS_UNITS_SV TO MAXDAT_REPORTS ;
