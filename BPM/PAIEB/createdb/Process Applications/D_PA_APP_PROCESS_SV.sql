CREATE OR REPLACE VIEW D_PA_APP_PROCESS_SV
AS
 SELECT /*+ parallel(5) */ ap.APP_PROCESS_ID AS APP_PROCESS_ID,
    ah.APPLICATION_ID AS APP_ID,
    ap.PROCESS_NAME,
    ap.PROCESS_INSTANCE_ID,
    ap.STATUS,
    ap.CREATE_TS,
    ap.CREATED_BY,
    ap.UPDATE_TS,
    ap.APP_INDIVIDUAL_ID,
    ap.UPDATED_BY
  FROM ATS.APP_HEADER ah
  LEFT JOIN ATS.APP_PROCESS ap ON (ah.APPLICATION_ID = ap.APPLICATION_ID)
  WHERE ah.application_id >= 343555
  AND ah.create_ts >= ADD_MONTHS(TRUNC(sysdate, 'mm'), -13)
;
    
GRANT SELECT ON D_PA_APP_PROCESS_SV TO MAXDAT_REPORTS;
