CREATE OR REPLACE VIEW D_PA_APP_MISSING_INFO_SV
AS
  SELECT MISSING_INFO_ID,
    APPLICATION_ID AS APP_ID,
    APP_INDIVIDUAL_ID,
    REPORTED_DATE,
    SATISFIED_DATE,
    CALL_RECORD_ID,
    CREATE_TS,
    CREATED_BY,
    UPDATE_TS,
    UPDATED_BY,
    MI_TYPE_CD,
    CLIENT_ID,
    SATISFIED_REASON_CD,
    SATISFIED_BY,
    VOIDED_BY,
    VOIDED_DATE,
    DUE_DATE,
    NOTIFICATION_DATE,
    MI_SOURCE_CD,
    SATISFIED_MEDIA_CD,
    NOTE_REF_ID,
    ENTITY_NAME,
    STATUS_CD,
    EXT_REF_ID
  FROM ATS.APP_MISSING_INFO ;
    
GRANT SELECT ON D_PA_APP_MISSING_INFO_SV TO MAXDAT_REPORTS;