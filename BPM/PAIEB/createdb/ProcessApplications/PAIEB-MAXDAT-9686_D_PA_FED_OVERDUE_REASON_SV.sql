
CREATE OR REPLACE VIEW MAXDAT_SUPPORT.D_PA_FED_OVERDUE_REASON_SV 
AS
SELECT
JOB_ID,
HARMONY_ID,
ETL_FED_ASSMT_HEADER_ID,
TRANSACTION_TYPE,
FED_OVERDUE_REASON_CODE,
FED_OVERDUE_REASON_COMMENTS,
CREATE_TS
FROM
ETL_L_FED_ASSMT_HEADER
WHERE TO_DATE(CREATE_TS) > '31-MAR-19';

GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_OVERDUE_REASON_SV TO MAXDATSUPPORT_READ_ONLY;

GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_OVERDUE_REASON_SV TO MAXDAT_REPORTS;
