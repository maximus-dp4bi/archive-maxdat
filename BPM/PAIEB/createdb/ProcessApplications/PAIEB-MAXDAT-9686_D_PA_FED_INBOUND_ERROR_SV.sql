
CREATE OR REPLACE VIEW MAXDAT_SUPPORT.D_PA_FED_INBOUND_ERROR_SV 
AS
SELECT 
H.JOB_ID,
H.ETL_FED_ASSMT_HEADER_ID,
H.TRANSACTION_TYPE,
H.FIRST_NAME,  
H.LAST_NAME,      
H.GENDER_CD,       
H.SSN,  
H.DOB,       
H.HARMONY_ID,
H.MCI_NUM,
H.ASSESSMENT_REQUEST_STATUS,
H.FED_REQ_DATE,
H.FED_COMPLETE_DATE, 
H.FED_ALGORITHM_RESULT,
E.ERROR_CODE,
E.ERROR_MESSAGE,
E.CREATE_TS
FROM ETL_L_FED_ASSMT_HEADER H
JOIN ETL_L_FED_ASSMT_ERROR E
ON H.ETL_FED_ASSMT_HEADER_ID = E.ETL_FED_ASSMT_HEADER_ID 
WHERE TO_DATE(E.CREATE_TS) > '31-MAR-19';


GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_INBOUND_ERROR_SV TO MAXDATSUPPORT_READ_ONLY;

GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_INBOUND_ERROR_SV TO MAXDAT_REPORTS;
