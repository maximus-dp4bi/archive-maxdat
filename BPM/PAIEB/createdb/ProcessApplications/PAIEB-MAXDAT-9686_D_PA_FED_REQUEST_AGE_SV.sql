
CREATE OR REPLACE VIEW MAXDAT_SUPPORT.D_PA_FED_REQUEST_AGE_SV 
AS
SELECT
FED_ASSMT_HEADER_ID,
FED_ASSMT_REQ_ID,
HARMONY_ID,
TRANSACTION_TYPE,
FIRST_NAME,
LAST_NAME,
MIDDLE_NAME,
GENDER_CD,
CASE_ID,
CLIENT_ID,
APPLICATION_ID,
CLIENT_CIN,
WITHDRAW_REASON,
WITHDRAW_REASON_CD,
JOB_ID,
CREATE_TS,
CASE WHEN TRANSACTION_TYPE IN ('CreateFED','ChangeProfile','WithdrawFED')
THEN TRUNC(sysdate) - TRUNC(create_ts)  ELSE NULL END AGE_IN_CAL_DAYS
FROM
FED_ASSESSMENT_OUT_HEADER 
WHERE TO_DATE(CREATE_TS) > '31-MAR-19';



GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_REQUEST_AGE_SV TO MAXDAT_SUPPORT_READ_ONLY;

GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_REQUEST_AGE_SV TO MAXDAT_REPORTS;

