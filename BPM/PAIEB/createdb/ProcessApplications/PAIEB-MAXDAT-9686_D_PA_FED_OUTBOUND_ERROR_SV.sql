
CREATE OR REPLACE VIEW MAXDAT_SUPPORT.D_PA_FED_OUTBOUND_ERROR_SV 
AS
SELECT A.ASSMT_RESP_HEADER_ID,
A.HARMONY_ID,
A.CLIENT_CIN,
A.PROCESSED_DATE,
B.ERROR_MESSAGE,
B.ERROR_TYPE,
B.CREATE_TS,
C.CLNT_FNAME AS FIRST_NAME,
C.CLNT_LNAME AS LAST_NAME
FROM FED_ASSMT_RESP_HEADER A
LEFT JOIN FED_ASSMT_RESP_ERROR B ON B.ASSMT_RESP_HEADER_ID = A.ASSMT_RESP_HEADER_ID
LEFT JOIN CLIENT C ON A.CLIENT_CIN = C.CLNT_CIN
WHERE TO_DATE(B.CREATE_TS) > '31-MAR-19';



GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_OUTBOUND_ERROR_SV TO MAXDAT_SUPPORT_READ_ONLY;

GRANT SELECT ON MAXDAT_SUPPORT.D_PA_FED_OUTBOUND_ERROR_SV TO MAXDAT_REPORTS;