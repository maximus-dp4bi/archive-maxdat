CREATE OR REPLACE VIEW REL_DOCUMENTS_STEP_INSTANCE_SV
AS
SELECT D.DCN,
  SI.STEP_INSTANCE_ID AS MW_BI_ID
FROM DOCUMENT D
JOIN DOCUMENT_SET DS             ON (D.DOCUMENT_SET_ID = DS.DOCUMENT_SET_ID)
JOIN APP_DOC_DATA A              ON (D.DOCUMENT_ID = A.DOCUMENT_ID)
LEFT OUTER JOIN STEP_INSTANCE SI ON (D.DOCUMENT_SET_ID=SI.REF_ID AND SI.REF_TYPE = 'DOCUMENT_SET_ID');

GRANT SELECT ON REL_DOCUMENTS_STEP_INSTANCE_SV TO MAXDAT_REPORTS;