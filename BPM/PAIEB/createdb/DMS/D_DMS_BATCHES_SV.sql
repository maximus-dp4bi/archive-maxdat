CREATE OR REPLACE VIEW D_DMS_BATCHES_SV
AS
  SELECT b.BATCH_ID,
  b.SOURCE,
  b.DATE_CAPTURED,
  b.CAPTURE_BATCH_ID,
  b.BATCH_CLASS,
  b.PRIORITY,
  COALESCE(b.NAME, 'No Batch') as BATCH_NAME,
  b.DESCRIPTION,
  b.DOCUMENT_COUNT,
  b.NUMBER_OF_PAGES,
  b.SCAN_STATION,
  b.SCAN_USERID,
  b.VALIDATION_STATION,
  b.VALIDATION_USERID,
  b.VERIFICATION_STATION,
  b.VERIFICATION_USERID,
  b.CREATION_DATE,
  b.CREATED_BY,
  b.LAST_UPDATED_BY,
  b.LAST_UPDATE_DATE,
  b.ORIGINAL_SYSTEM_NAME
  from PAIEDMS.BATCHES b;

GRANT SELECT ON D_DMS_BATCHES_SV TO MAXDAT_REPORTS;

