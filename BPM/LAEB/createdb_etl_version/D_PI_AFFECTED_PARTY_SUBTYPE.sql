CREATE OR REPLACE TRIGGER "BIR_AFFECTED_PARTY_SUBTYPE" 
 BEFORE INSERT
 ON d_pi_affected_party_subtype
 FOR EACH ROW
DECLARE
    v_seq_id     D_PI_AFFECTED_PARTY_SUBTYPE.affected_party_subtype_id%TYPE;
BEGIN
  IF :NEW.affected_party_subtype_id IS NULL THEN
    SElECT D_SEQ_AFFPARTY_SUBTYPE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.affected_party_subtype_id       := v_seq_id;
  END IF;
END BIR_AFFECTED_PARTY_SUBTYPE;
/
ALTER TRIGGER "BIR_AFFECTED_PARTY_SUBTYPE" ENABLE;
/

