CREATE OR REPLACE TRIGGER "BIR_INCIDENT_STATUS" 
 BEFORE INSERT
 ON d_pi_incident_status
 FOR EACH ROW
DECLARE
    v_seq_id     D_PI_INCIDENT_STATUS.incident_status_id%TYPE;
BEGIN
  IF :NEW.incident_status_id IS NULL THEN
    SElECT D_SEQ_INCIDENT_STATUS_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.incident_status_id       := v_seq_id;
  END IF;
END BIR_INCIDENT_STATUS;
/
ALTER TRIGGER "BIR_INCIDENT_STATUS" ENABLE;
/

