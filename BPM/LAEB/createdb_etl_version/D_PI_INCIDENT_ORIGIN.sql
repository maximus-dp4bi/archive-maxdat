CREATE OR REPLACE TRIGGER "BIR_INCIDENT_ORIGIN" 
 BEFORE INSERT
 ON d_pi_incident_origin
 FOR EACH ROW
DECLARE
    v_seq_id     D_PI_INCIDENT_ORIGIN.incident_origin_id%TYPE;
BEGIN
  IF :NEW.incident_origin_id IS NULL THEN
    SElECT D_SEQ_INCIDENT_ORIGIN_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.incident_origin_id       := v_seq_id;
  END IF;
END BIR_INCIDENT_ORIGIN;
/
ALTER TRIGGER "BIR_INCIDENT_ORIGIN" ENABLE;
/

