CREATE SEQUENCE  "EMRS_SEQ_CSDA_ID"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1061 CACHE 20 NOORDER  NOCYCLE ;

GRANT SELECT ON "EMRS_SEQ_CSDA_ID" TO &role_name;

CREATE TABLE EMRS_D_CARE_SERV_DELIV_AREA
   (CSDA_ID NUMBER NOT NULL, 
    CSDA_CODE VARCHAR2(32),
    CSDA_NAME VARCHAR2(240),
    REGION_ID NUMBER,
    STATUS VARCHAR2(1),
    EFFECTIVE_START_DATE DATE,
    EFFECTIVE_END_DATE DATE
	  ) TABLESPACE &tablespace_name ;

 ALTER TABLE "EMRS_D_CARE_SERV_DELIV_AREA" ADD CONSTRAINT "CSDA_COMB_UK" UNIQUE ("CSDA_CODE") USING INDEX  TABLESPACE &tablespace_name  ENABLE;
 ALTER TABLE "EMRS_D_CARE_SERV_DELIV_AREA" ADD CONSTRAINT "CSDAPK" PRIMARY KEY ("CSDA_ID") USING INDEX TABLESPACE &tablespace_name  ENABLE;
  
GRANT SELECT ON "EMRS_D_CARE_SERV_DELIV_AREA" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_CARE_SERV_DELIV_AREA" 
 BEFORE INSERT
 ON emrs_d_care_serv_deliv_area
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_CARE_SERV_DELIV_AREA.csda_id%TYPE;
BEGIN
  IF :NEW.csda_id IS NULL THEN
    SElECT EMRS_SEQ_CSDA_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.csda_id       := v_seq_id;
  END IF;
END BIR_CARE_SERV_DELIV_AREA;
/
ALTER TRIGGER "BIR_CARE_SERV_DELIV_AREA" ENABLE;
/

CREATE OR REPLACE VIEW EMRS_D_CARE_SERV_DELIV_AREA_SV
AS
SELECT csda_code
       ,csda_name
       ,region_id
       ,status
       ,effective_start_date
       ,effective_end_date
FROM emrs_d_care_serv_deliv_area;

GRANT SELECT ON "EMRS_D_CARE_SERV_DELIV_AREA_SV" TO &role_name;
