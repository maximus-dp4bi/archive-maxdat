CREATE OR REPLACE TRIGGER "BIR_ADDR_TYPE" 
 BEFORE INSERT
 ON emrs_d_address_type
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_ADDRESS_TYPE.address_type_id%TYPE;
BEGIN
  IF :NEW.address_type_id IS NULL THEN
    SElECT EMRS_SEQ_ADDRESS_TYPE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.address_type_id       := v_seq_id;
  END IF;
END BIR_ADDR_TYPE;
/
ALTER TRIGGER "BIR_ADDR_TYPE" ENABLE;
/

