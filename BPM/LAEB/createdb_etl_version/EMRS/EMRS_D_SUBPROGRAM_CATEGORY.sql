CREATE OR REPLACE TRIGGER "BIR_SUBPROGCAT"
 BEFORE INSERT
 ON emrs_d_subprogram_category
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_SUBPROGRAM_CATEGORY.subprogram_category_id%TYPE;
BEGIN
  IF :NEW.subprogram_category_id IS NULL THEN
    SElECT EMRS_SEQ_SPCAT_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.subprogram_category_id       := v_seq_id;
  END IF;
END BIR_SUBPROGCAT;
/
ALTER TRIGGER "BIR_SUBPROGCAT" ENABLE;
/

