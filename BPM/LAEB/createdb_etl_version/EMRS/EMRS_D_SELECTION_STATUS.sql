  (SELECTION_STATUS_ID NUMBER NOT NULL, 
   SELECTION_STATUS_CODE VARCHAR2(32), 
   SELECTION_STATUS_DESCRIPTION VARCHAR2(240), 	
   OVERWRITE_IND NUMBER(1,0),
   OVERWRITE_TO_VALUE VARCHAR2(32),
   VALID_SELECTION_IND NUMBER(1,0),
   ALLOW_NEW_SELECTION_IND NUMBER(1,0)
   ) TABLESPACE &tablespace_name ;

ALTER TABLE "EMRS_D_SELECTION_STATUS" ADD CONSTRAINT "SLCTSTATUS_UK" UNIQUE ("SELECTION_STATUS_CODE") USING INDEX TABLESPACE &tablespace_name  ENABLE; 
ALTER TABLE "EMRS_D_SELECTION_STATUS" ADD CONSTRAINT "SLCTSTATUSPK" PRIMARY KEY ("SELECTION_STATUS_ID") USING INDEX TABLESPACE &tablespace_name  ENABLE;
  
GRANT SELECT ON "EMRS_D_SELECTION_STATUS" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_SELECTION_STATUS" 
 BEFORE INSERT
 ON emrs_d_selection_status
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_SELECTION_STATUS.selection_status_id%TYPE;
BEGIN
  IF :NEW.selection_status_id IS NULL THEN
    SElECT EMRS_SEQ_SELECTION_STATUS_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.selection_status_id   := v_seq_id;
  END IF;
END BIR_SELECTION_STATUS;
/
ALTER TRIGGER "BIR_SELECTION_STATUS" ENABLE;
/


CREATE OR REPLACE VIEW EMRS_D_SELECTION_STATUS_SV
AS
SELECT selection_status_code
       ,selection_status_description
       ,overwrite_ind
       ,overwrite_to_value
       ,valid_selection_ind
       ,allow_new_selection_ind       
FROM emrs_d_selection_status;

GRANT SELECT ON "EMRS_D_SELECTION_STATUS_SV" TO &role_name;


