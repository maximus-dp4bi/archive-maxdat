GRANT SELECT ON "EMRS_D_ENROLLMENT_TYPE" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_ENROLL_TYPE" 
 BEFORE INSERT
 ON emrs_d_enrollment_type
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_ENROLLMENT_TYPE.enrollment_type_id%TYPE;
BEGIN
  IF :NEW.enrollment_type_id IS NULL THEN
    SElECT EMRS_SEQ_ENROLL_TYPE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.enrollment_type_id       := v_seq_id;
  END IF;
END BIR_ENROLL_TYPE;
/
ALTER TRIGGER "BIR_ENROLL_TYPE" ENABLE;
/

CREATE OR REPLACE VIEW EMRS_D_ENROLL_TYPE_SV
AS
SELECT enrollment_type_code
       ,enrollment_type
       ,order_by_default
FROM emrs_d_enrollment_type;

GRANT SELECT ON "EMRS_D_ENROLL_TYPE_SV" TO &role_name;
