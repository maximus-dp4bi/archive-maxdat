CREATE SEQUENCE  "EMRS_SEQ_CHANNEL_ID"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1061 CACHE 20 NOORDER  NOCYCLE ;

GRANT SELECT ON "EMRS_SEQ_CHANNEL_ID" TO &role_name;

CREATE TABLE EMRS_D_CHANNEL
   (CHANNEL_ID NUMBER NOT NULL, 
    CHANNEL_CODE VARCHAR2(32),
    CHANNEL VARCHAR2(240)  
	  ) TABLESPACE &tablespace_name ;

 ALTER TABLE "EMRS_D_CHANNEL" ADD CONSTRAINT "CHANNEL_COMB_UK" UNIQUE ("CHANNEL_CODE") USING INDEX  TABLESPACE &tablespace_name  ENABLE;
 ALTER TABLE "EMRS_D_CHANNEL" ADD CONSTRAINT "CHANNELPK" PRIMARY KEY ("CHANNEL_ID") USING INDEX TABLESPACE &tablespace_name  ENABLE;
  
GRANT SELECT ON "EMRS_D_CHANNEL" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_CHANNEL" 
 BEFORE INSERT
 ON emrs_d_channel
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_CHANNEL.channel_id%TYPE;
BEGIN
  IF :NEW.channel_id IS NULL THEN
    SElECT EMRS_SEQ_CHANNEL_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.channel_id       := v_seq_id;
  END IF;
END BIR_CHANNEL;
/
ALTER TRIGGER "BIR_CHANNEL" ENABLE;
/

CREATE OR REPLACE VIEW EMRS_D_CHANNEL_SV
AS
SELECT channel_code
       ,channel
FROM emrs_d_channel;

