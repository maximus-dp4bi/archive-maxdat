CREATE OR REPLACE TRIGGER "BIR_BENEFITS_PKG" 
 BEFORE INSERT
 ON emrs_d_benefits_package
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_BENEFITS_PACKAGE.benefits_package_id%TYPE;
BEGIN
  IF :NEW.benefits_package_id IS NULL THEN
    SElECT EMRS_SEQ_BENEFITS_PKG_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.benefits_package_id       := v_seq_id;
  END IF;
END BIR_BENEFITS_PKG;
/
ALTER TRIGGER "BIR_BENEFITS_PKG" ENABLE;
/

