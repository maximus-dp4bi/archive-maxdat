  CREATE OR REPLACE TRIGGER "BIR_LANGUAGE" 
 BEFORE INSERT
 ON emrs_d_language
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_LANGUAGE.language_id%TYPE;
BEGIN
  IF :NEW.language_id IS NULL THEN
    SElECT EMRS_SEQ_LANGUAGE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.language_id       := v_seq_id;
  END IF;
END BIR_LANGUAGE;
/
ALTER TRIGGER "BIR_LANGUAGE" ENABLE;
/


