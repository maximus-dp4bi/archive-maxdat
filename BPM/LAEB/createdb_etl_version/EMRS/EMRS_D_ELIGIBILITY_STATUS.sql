CREATE OR REPLACE TRIGGER "BIR_ELIGSTATUS"
 BEFORE INSERT
 ON emrs_d_eligibility_status
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_ELIGIBILITY_STATUS.eligibility_status_id%TYPE;
BEGIN
  IF :NEW.eligibility_status_id IS NULL THEN
    SElECT EMRS_SEQ_ELIGSTAT_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.eligibility_status_id       := v_seq_id;
  END IF;
END BIR_ELIGSTATUS;
/
ALTER TRIGGER "BIR_ELIGSTATUS" ENABLE;
/

