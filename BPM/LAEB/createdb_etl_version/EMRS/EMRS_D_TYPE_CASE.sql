CREATE SEQUENCE  "EMRS_SEQ_TYPECASE_ID"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1061 CACHE 20 NOORDER  NOCYCLE ;

GRANT SELECT ON "EMRS_SEQ_TYPECASE_ID" TO &role_name;

CREATE TABLE EMRS_D_TYPE_CASE
   (TYPE_CASE_ID NUMBER NOT NULL, 
    TYPE_CASE_CODE VARCHAR2(32),
    TYPE_CASE VARCHAR2(240)    
	  ) TABLESPACE &tablespace_name ;

 ALTER TABLE "EMRS_D_TYPE_CASE" ADD CONSTRAINT "TYPECASE_COMB_UK" UNIQUE ("TYPE_CASE_CODE") USING INDEX  TABLESPACE &tablespace_name  ENABLE;
 ALTER TABLE "EMRS_D_TYPE_CASE" ADD CONSTRAINT "TYPECASEPK" PRIMARY KEY ("TYPE_CASE_ID") USING INDEX TABLESPACE &tablespace_name  ENABLE;
  
GRANT SELECT ON "EMRS_D_TYPE_CASE" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_TYPECASE"
 BEFORE INSERT
 ON emrs_d_type_case
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_TYPE_CASE.type_case_id%TYPE;
BEGIN
  IF :NEW.type_case_id IS NULL THEN
    SElECT EMRS_SEQ_TYPECASE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.type_case_id       := v_seq_id;
  END IF;
END BIR_TYPECASE;
/
ALTER TRIGGER "BIR_TYPECASE" ENABLE;
/

CREATE OR REPLACE VIEW EMRS_D_TYPE_CASE_SV
AS
SELECT type_case_code
       ,type_case type_case_description
FROM emrs_d_type_case;

GRANT SELECT ON "EMRS_D_TYPE_CASE_SV" TO &role_name;
