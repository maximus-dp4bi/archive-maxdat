CREATE OR REPLACE TRIGGER "BIR_GENDER" 
 BEFORE INSERT
 ON emrs_d_gender
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_GENDER.gender_id%TYPE;
BEGIN
  IF :NEW.gender_id IS NULL THEN
    SElECT EMRS_SEQ_GENDER_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.gender_id       := v_seq_id;
  END IF;
END BIR_GENDER;
/
ALTER TRIGGER "BIR_GENDER" ENABLE;
/

