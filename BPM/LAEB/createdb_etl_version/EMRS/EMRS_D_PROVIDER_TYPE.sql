CREATE TABLE EMRS_D_PROVIDER_TYPE
   (PROVIDER_TYPE_ID NUMBER NOT NULL, 
    PROVIDER_TYPE_CODE VARCHAR2(32),
    PROVIDER_TYPE_NAME VARCHAR2(240),
    EFFECTIVE_START_DATE DATE,
    EFFECTIVE_END_DATE DATE
	  ) TABLESPACE &tablespace_name ;

 ALTER TABLE "EMRS_D_PROVIDER_TYPE" ADD CONSTRAINT "PROVTYPE_COMB_UK" UNIQUE ("PROVIDER_TYPE_CODE") USING INDEX  TABLESPACE &tablespace_name  ENABLE;
 ALTER TABLE "EMRS_D_PROVIDER_TYPE" ADD CONSTRAINT "PROVTYPEPK" PRIMARY KEY ("PROVIDER_TYPE_ID") USING INDEX TABLESPACE &tablespace_name  ENABLE;
  
GRANT SELECT ON "EMRS_D_PROVIDER_TYPE" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_PROVIDER_TYPE" 
 BEFORE INSERT
 ON emrs_d_provider_type
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_PROVIDER_TYPE.provider_type_id%TYPE;
BEGIN
  IF :NEW.provider_type_id IS NULL THEN
    SElECT EMRS_SEQ_PROVTYPE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.provider_type_id       := v_seq_id;
  END IF;
END BIR_PROVIDER_TYPE;
/
ALTER TRIGGER "BIR_PROVIDER_TYPE" ENABLE;
/

CREATE OR REPLACE VIEW EMRS_D_PROVIDER_TYPE_SV
AS
SELECT provider_type_code
       ,provider_type_name
       ,effective_start_date
       ,effective_end_date
FROM emrs_d_provider_type;

GRANT SELECT ON "EMRS_D_PROVIDER_TYPE_SV" TO &role_name;
