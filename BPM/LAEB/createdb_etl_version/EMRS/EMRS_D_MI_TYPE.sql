CREATE OR REPLACE TRIGGER "BIR_MI_TYPE" 
 BEFORE INSERT
 ON emrs_d_mi_type
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_MI_TYPE.mi_type_id%TYPE;
BEGIN
  IF :NEW.mi_type_id IS NULL THEN
    SElECT EMRS_SEQ_MI_TYPE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.mi_type_id       := v_seq_id;
  END IF;
END BIR_MI_TYPE;
/
ALTER TRIGGER "BIR_MI_TYPE" ENABLE;
/

