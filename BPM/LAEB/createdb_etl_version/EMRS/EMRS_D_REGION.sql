GRANT SELECT ON "EMRS_D_REGION" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_REGION"
 BEFORE INSERT
 ON emrs_d_region
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_REGION.region_id%TYPE;
BEGIN
  IF :NEW.region_id IS NULL THEN
    SElECT EMRS_SEQ_REGION_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.region_id       := v_seq_id;
  END IF;
END BIR_REGION;
/
ALTER TRIGGER "BIR_REGION" ENABLE;
/

CREATE OR REPLACE VIEW EMRS_D_REGION_SV
AS
SELECT TO_NUMBER(region_code) region_id,region_name
FROM emrs_d_region;

