 ALTER TABLE "EMRS_D_RELATIONSHIP" ADD CONSTRAINT "RELATIONSHIPPK" PRIMARY KEY ("RELATIONSHIP_ID") USING INDEX TABLESPACE &tablespace_name  ENABLE;
  
GRANT SELECT ON "EMRS_D_RELATIONSHIP" TO &role_name;

CREATE OR REPLACE TRIGGER "BIR_RELATIONSHIP"
 BEFORE INSERT
 ON emrs_d_relationship
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_RELATIONSHIP.relationship_id%TYPE;
BEGIN
  IF :NEW.relationship_id IS NULL THEN
    SElECT EMRS_SEQ_RELATIONSHIP_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.relationship_id       := v_seq_id;
  END IF;
END BIR_RELATIONSHIP;
/
ALTER TRIGGER "BIR_RELATIONSHIP" ENABLE;
/

CREATE OR REPLACE VIEW EMRS_D_RELATIONSHIP_SV
AS
SELECT relationship_code
       ,relationship
FROM emrs_d_relationship;

