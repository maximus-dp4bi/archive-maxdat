DROP VIEW MAXDAT_SUPPORT.EMRS_D_ADDR_TYPE_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.EMRS_D_ADDR_TYPE_SV
AS
SELECT value AS ADDR_TYPE_CD
      ,report_label AS ADDR_TYPE
FROM eb.enum_address_type
WHERE effective_end_date IS NULL
UNION ALL
SELECT 'UD' AS ADDR_TYPE_CD
      , 'Undefined' AS ADDR_TYPE
FROM DUAL;

GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_ADDR_TYPE_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_ADDR_TYPE_SV TO GT83345;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_ADDR_TYPE_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_ADDR_TYPE_SV TO MAXDAT_SUPPORT_READ_ONLY;

