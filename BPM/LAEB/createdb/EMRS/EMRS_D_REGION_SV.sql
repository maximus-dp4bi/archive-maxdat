DROP VIEW MAXDAT_SUPPORT.EMRS_D_REGION_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.EMRS_D_REGION_SV
AS
SELECT 1 AS region_id, 'Capital' AS region_name FROM DUAL
    UNION ALL
    SELECT 2 AS region_id, 'Gulf' AS region_name FROM DUAL
    UNION ALL
    SELECT 3 AS region_id, 'North' AS region_name FROM DUAL
    UNION ALL
    SELECT 4 AS region_id, 'South Central' AS region_name FROM DUAL
    UNION ALL
    SELECT -1 AS region_id, 'Unknown' AS region_name FROM DUAL;

GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_REGION_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_REGION_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_REGION_SV TO MAXDAT_SUPPORT_READ_ONLY;
