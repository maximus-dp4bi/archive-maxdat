DROP VIEW MAXDAT_SUPPORT.EMRS_D_PROVIDER_SPECIALTY_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.EMRS_D_PROVIDER_SPECIALTY_SV
AS
SELECT network_id AS PROVIDER_NUMBER ,
    COALESCE(specialty_type_cd, '0') AS PROVIDER_SPECIALTY_CODE,
    create_ts AS DATE_CREATED ,
    created_by AS CREATED_BY,
    update_ts AS DATE_UPDATED,
    updated_by AS UPDATED_BY
  FROM eb.network_specialty;

GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_PROVIDER_SPECIALTY_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_PROVIDER_SPECIALTY_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_PROVIDER_SPECIALTY_SV TO MAXDAT_SUPPORT_READ_ONLY;
