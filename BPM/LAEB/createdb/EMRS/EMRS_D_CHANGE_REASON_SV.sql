DROP VIEW MAXDAT_SUPPORT.EMRS_D_CHANGE_REASON_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.EMRS_D_CHANGE_REASON_SV
AS
SELECT 
    edr.value AS CHANGE_REASON_CODE,
    edr.report_label AS CHANGE_REASON,
    NULL AS CHANGE_REASON_CODE_PLAN
  FROM eb.enum_disenrollment_reason edr
  UNION
  SELECT
    '0' CHANGE_REASON_CODE ,
    'Not Defined' CHANGE_REASON ,
    NULL CHANGE_REASON_CODE_PLAN
  FROM DUAL;

GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_CHANGE_REASON_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_CHANGE_REASON_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_CHANGE_REASON_SV TO MAXDAT_SUPPORT_READ_ONLY;
