DROP VIEW MAXDAT_SUPPORT.EMRS_D_MI_TYPE_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.EMRS_D_MI_TYPE_SV
AS
SELECT m.value AS MISSING_INFO_TYPE_CD ,
    m.description AS MISSING_INFO_TYPE  
  FROM EB.ENUM_MISSING_INFO_TYPE m
  UNION
  SELECT '0' AS MISSING_INFO_TYPE_CD ,
    'Not Defined' AS MISSING_INFO_TYPE  
  FROM DUAL;

GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_MI_TYPE_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_MI_TYPE_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_MI_TYPE_SV TO MAXDAT_SUPPORT_READ_ONLY;
