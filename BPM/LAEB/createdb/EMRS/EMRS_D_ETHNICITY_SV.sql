DROP VIEW MAXDAT_SUPPORT.EMRS_D_ETHNICITY_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.EMRS_D_ETHNICITY_SV
AS
SELECT 
    e.VALUE AS CLIENT_ETHNICITY
    ,e.REPORT_LABEL AS ETHNICITY
    ,e.EFFECTIVE_START_DATE AS EFFECTIVE_START_DATE
    ,e.EFFECTIVE_END_DATE AS EFFECTIVE_END_DATE
  FROM EB.ENUM_ETHNICITY e;

GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_ETHNICITY_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_ETHNICITY_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_ETHNICITY_SV TO MAXDAT_SUPPORT_READ_ONLY;

