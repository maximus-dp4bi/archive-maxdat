DROP VIEW MAXDAT_SUPPORT.EMRS_D_PHONE_TYPE_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.EMRS_D_PHONE_TYPE_SV
AS
SELECT value AS PHON_TYPE_CD
      ,report_label AS PHONE_TYPE
FROM eb.enum_phone_type
WHERE effective_end_date IS NULL
UNION ALL
SELECT 'UD' AS PHONE_TYP_CD
      , 'Undefined' AS PHONE_TYPE
FROM DUAL;

GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_PHONE_TYPE_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_PHONE_TYPE_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_PHONE_TYPE_SV TO MAXDAT_SUPPORT_READ_ONLY;
