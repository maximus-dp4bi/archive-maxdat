DROP VIEW MAXDAT_SUPPORT.D_PI_INCIDENT_ORIGIN_SV;

CREATE OR REPLACE VIEW MAXDAT_SUPPORT.D_PI_INCIDENT_ORIGIN_SV
AS
SELECT value AS INCIDENT_ORIGIN_CD
      , description AS INCIDENT_ORIGIN
FROM eb.ENUM_INCIDENT_ORIGIN
UNION ALL
SELECT 'UD' AS INCIDENT_ORIGIN_CD
      , 'Undefined' AS INCIDENT_ORIGIN
FROM DUAL;

GRANT SELECT ON MAXDAT_SUPPORT.D_PI_INCIDENT_ORIGIN_SV TO DP4BI_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.D_PI_INCIDENT_ORIGIN_SV TO MAXDATSUPPORT_READ_ONLY;
GRANT SELECT ON MAXDAT_SUPPORT.D_PI_INCIDENT_ORIGIN_SV TO MAXDAT_SUPPORT_READ_ONLY;
