   ) TABLESPACE MAXDAT_DATA ;

CREATE INDEX HCOSMAILSTG_IDX01 ON HCO_S_MAILING_STG(MAIL_ID) TABLESPACE MAXDAT_INDX;

CREATE TABLE HCO_D_MAILING
(MAIL_ID	VARCHAR2(10),
CUSTOM_MAILING_INDICATOR VARCHAR2(10),
RECORD_DATE DATE
   ) TABLESPACE MAXDAT_DATA ;
   
CREATE UNIQUE INDEX HCODMAIL_UKIDX ON HCO_D_MAILING(MAIL_ID) TABLESPACE MAXDAT_INDX;
  
GRANT SELECT ON "HCO_D_MAILING" TO "MAXDAT_READ_ONLY";
