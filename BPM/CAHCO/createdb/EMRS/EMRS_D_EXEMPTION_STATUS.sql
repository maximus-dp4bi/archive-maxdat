GRANT SELECT ON "EMRS_D_EXEMPTION_STATUS" TO "MAXDAT_READ_ONLY";

CREATE OR REPLACE TRIGGER "BIR_EXEMPTION_STATUS" 
 BEFORE INSERT
 ON emrs_d_exemption_status
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_EXEMPTION_STATUS.exemption_status_id%TYPE;
BEGIN
  IF :NEW.exemption_status_id IS NULL THEN
    SElECT EMRS_SEQ_EXEMPTION_STATUS_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.exemption_status_id       := v_seq_id;
  END IF;
END BIR_EXEMPTION_STATUS;
/
ALTER TRIGGER "BIR_EXEMPTION_STATUS" ENABLE;
/


CREATE OR REPLACE VIEW EMRS_D_EXEMPTION_STATUS_SV
AS
SELECT exemption_status_id
       ,exemption_status_code
       ,exemption_status
       ,final_status
       ,approval
       ,denial
       ,pending
FROM emrs_d_exemption_status;

GRANT SELECT ON "EMRS_D_EXEMPTION_STATUS_SV" TO "MAXDAT_READ_ONLY";