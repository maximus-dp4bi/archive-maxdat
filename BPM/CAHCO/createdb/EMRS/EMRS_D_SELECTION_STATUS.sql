CREATE SEQUENCE "EMRS_SEQ_SELECTION_STATUS_ID" MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 86401 CACHE 5 NOORDER  NOCYCLE ;

GRANT SELECT ON "EMRS_SEQ_SELECTION_STATUS_ID" TO "MAXDAT_READ_ONLY";

CREATE TABLE EMRS_D_SELECTION_STATUS
  (SELECTION_STATUS_ID NUMBER NOT NULL, 
	 SELECTION_STATUS_CODE VARCHAR2(32), 
	 SELECTION_STATUS_DESCRIPTION VARCHAR2(240), 	
	 ALLOW_NEW_SELECTION_IND VARCHAR2(1)
   ) TABLESPACE MAXDAT_DATA ;

ALTER TABLE "EMRS_D_SELECTION_STATUS" ADD CONSTRAINT "SLCTSTATUS_UK" UNIQUE ("SELECTION_STATUS_CODE") USING INDEX TABLESPACE "MAXDAT_INDX"  ENABLE; 
ALTER TABLE "EMRS_D_SELECTION_STATUS" ADD CONSTRAINT "SLCTSTATUSPK" PRIMARY KEY ("SELECTION_STATUS_ID") USING INDEX TABLESPACE "MAXDAT_INDX"  ENABLE;
  
GRANT SELECT ON "EMRS_D_SELECTION_STATUS" TO "MAXDAT_READ_ONLY";

CREATE OR REPLACE TRIGGER "BIR_SELECTION_STATUS" 
 BEFORE INSERT
 ON emrs_d_selection_status
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_SELECTION_STATUS.selection_status_id%TYPE;
BEGIN
  IF :NEW.selection_status_id IS NULL THEN
    SElECT EMRS_SEQ_SELECTION_STATUS_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.selection_status_id   := v_seq_id;
  END IF;
END BIR_SELECTION_STATUS;
/
ALTER TRIGGER "BIR_SELECTION_STATUS" ENABLE;
/


CREATE OR REPLACE VIEW EMRS_D_SELECTION_STATUS_SV
AS
SELECT selection_status_id
       ,selection_status_code
       ,selection_status_description
       ,allow_new_selection_ind       
FROM emrs_d_selection_status;

GRANT SELECT ON "EMRS_D_SELECTION_STATUS_SV" TO "MAXDAT_READ_ONLY";


