   (AID_CATEGORY_ID NUMBER NOT NULL, 
    AID_CATEGORY_CODE VARCHAR2(32),
    AID_CATEGORY_NAME VARCHAR2(240),     
    ACTIVE_INACTIVE VARCHAR2(1) DEFAULT 'N', 
    START_DATE DATE,
    END_DATE DATE
