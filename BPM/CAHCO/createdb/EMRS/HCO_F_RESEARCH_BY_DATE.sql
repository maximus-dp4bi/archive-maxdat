CREATE TABLE MAXDAT.HCO_F_RESEARCH_BY_DATE
    (
      REFERRAL_DATE             DATE
    , TCC_ISSUES_REFERRED   NUMBER (7,0)
    , ISSUES_CLOSED         NUMBER (7,0)
    , TCC_ISSUES_REFERRED_BY_TYPE NUMBER (7,0),
    CONSTRAINT REFERRAL_DATE_PK PRIMARY KEY (REFERRAL_DATE)
    )
    TABLESPACE MAXDAT_DATA
    STORAGE (BUFFER_POOL DEFAULT);


GRANT SELECT ON MAXDAT.HCO_F_RESEARCH_BY_DATE TO MAXDAT_READ_ONLY;

CREATE OR REPLACE VIEW HCO_F_RESEARCH_BY_DATE_SV AS
SELECT
    REFERRAL_DATE
    , TCC_ISSUES_REFERRED
    , ISSUES_CLOSED
    , TCC_ISSUES_REFERRED_BY_TYPE
FROM MAXDAT.HCO_F_RESEARCH_BY_DATE
WITH READ ONLY;

GRANT SELECT ON MAXDAT.HCO_F_RESEARCH_BY_DATE_SV TO MAXDAT_READ_ONLY;
