CREATE OR REPLACE TRIGGER "BIR_FORMBENESTATUS" 
 BEFORE INSERT
 ON hco_d_form_bene_status
 FOR EACH ROW
DECLARE
    v_seq_id     HCO_D_FORM_BENE_STATUS.dp_form_bene_status_id%TYPE;
BEGIN
  IF :NEW.dp_form_bene_status_id IS NULL THEN
    SElECT HCO_SEQ_FORMBENESTAT_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.dp_form_bene_status_id       := v_seq_id;
  END IF;
END BIR_FORMBENESTATUS;
/
ALTER TRIGGER "BIR_FORMBENESTATUS" ENABLE;
/

