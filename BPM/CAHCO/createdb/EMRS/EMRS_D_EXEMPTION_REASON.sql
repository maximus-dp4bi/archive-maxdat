CREATE OR REPLACE TRIGGER "BIR_EXEMPTION_REASON" 
 BEFORE INSERT
 ON emrs_d_exemption_reason
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_EXEMPTION_REASON.exemption_reason_id%TYPE;
BEGIN
  IF :NEW.exemption_reason_id IS NULL THEN
    SElECT EMRS_SEQ_EXEMPTION_REASON_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.exemption_reason_id       := v_seq_id;
  END IF;
END BIR_EXEMPTION_REASON;
/
ALTER TRIGGER "BIR_EXEMPTION_REASON" ENABLE;
/


