CREATE OR REPLACE TRIGGER "BIR_EMRGCY_DENR_SOURCE" 
 BEFORE INSERT
 ON emrs_d_emrgcy_denr_source
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_EMRGCY_DENR_SOURCE.emrgcy_denr_source_id%TYPE;
BEGIN
  IF :NEW.emrgcy_denr_source_id IS NULL THEN
    SElECT EMRS_SEQ_EMRGCY_DENR_SOURCE_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.emrgcy_denr_source_id       := v_seq_id;
  END IF;
END BIR_EMRGCY_DENR_SOURCE;
/
ALTER TRIGGER "BIR_EMRGCY_DENR_SOURCE" ENABLE;
/

