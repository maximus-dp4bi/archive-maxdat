CREATE OR REPLACE TRIGGER "BIR_EMRGCY_DENR_REASON" 
 BEFORE INSERT
 ON emrs_d_emrgcy_denr_reason
 FOR EACH ROW
DECLARE
    v_seq_id     EMRS_D_EMRGCY_DENR_REASON.emrgcy_denr_reason_id%TYPE;
BEGIN
  IF :NEW.emrgcy_denr_reason_id IS NULL THEN
    SElECT EMRS_SEQ_EMRGCY_DENR_REASON_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.emrgcy_denr_reason_id       := v_seq_id;
  END IF;
END BIR_EMRGCY_DENR_REASON;
/
ALTER TRIGGER "BIR_EMRGCY_DENR_REASON" ENABLE;
/


