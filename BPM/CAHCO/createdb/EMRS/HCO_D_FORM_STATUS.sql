CREATE OR REPLACE TRIGGER "BIR_FORMSTATUS" 
 BEFORE INSERT
 ON hco_d_form_status
 FOR EACH ROW
DECLARE
    v_seq_id     HCO_D_FORM_STATUS.dp_form_status_id%TYPE;
BEGIN
  IF :NEW.dp_form_status_id IS NULL THEN
    SElECT HCO_SEQ_FORMSTAT_ID.NEXTVAL
    INTO v_seq_id
    FROM dual;

    :NEW.dp_form_status_id       := v_seq_id;
  END IF;
END BIR_FORMSTATUS;
/
ALTER TRIGGER "BIR_FORMSTATUS" ENABLE;
/

