alter table D_NYHIX_MFD_CURRENT rename constraint NYHIX_MFD_PK to DNMFD_PK;

create index FNMFDBD_IXL4 on F_NYHIX_MFD_BY_DATE (CREATION_COUNT) local online tablespace MAXDAT_INDX parallel compute statistics;

create or replace view F_NYHIX_MFD_BY_DATE_SV as
-- First day plus interpolate days until before the next day with an update.
select
  FNMFDBD_ID,
  BUCKET_START_DATE D_DATE,
  NYHIX_MFD_BI_ID,
  DNMFDDT_ID,
  DNMFDDS_ID,
  DNMFDES_ID,
  DNMFDDST_ID,
  DNMFDFT_ID,
  DNMFDTS_ID,
  DNMFDIS_ID,
  INSTANCE_STATUS_DT,
  DOCUMENT_STATUS_DT,
  RECEIPT_DT,
  SCAN_DT,
  RELEASE_DT,
  JEOPARDY_FLAG,
  CREATION_COUNT,
  INVENTORY_COUNT,
  COMPLETION_COUNT
from F_NYHIX_MFD_BY_DATE
where
  D_DATE >= (select min(D_DATE) from BPM_D_DATES)
  and CREATION_COUNT = 1
union all
(
-- First day (again) and all days with interpolated days in-between, except completion day.
select
  FNMFDBD_ID,
  bdd.D_DATE,
  NYHIX_MFD_BI_ID,
  DNMFDDT_ID,
  DNMFDDS_ID,
  DNMFDES_ID,
  DNMFDDST_ID,
  DNMFDFT_ID,
  DNMFDTS_ID,
  DNMFDIS_ID,
  INSTANCE_STATUS_DT,
  DOCUMENT_STATUS_DT,
  RECEIPT_DT,
  SCAN_DT,
  RELEASE_DT,
  JEOPARDY_FLAG,
  0 CREATION_COUNT,
  INVENTORY_COUNT,
  COMPLETION_COUNT
from 
  F_NYHIX_MFD_BY_DATE,
  BPM_D_DATES bdd
where
  bdd.D_DATE >= BUCKET_START_DATE
  and bdd.D_DATE < BUCKET_END_DATE
minus
-- Remove duplicate first day.
select
  FNMFDBD_ID,
  bdd.D_DATE,
  NYHIX_MFD_BI_ID,
  DNMFDDT_ID,
  DNMFDDS_ID,
  DNMFDES_ID,
  DNMFDDST_ID,
  DNMFDFT_ID,
  DNMFDTS_ID,
  DNMFDIS_ID,
  INSTANCE_STATUS_DT,
  DOCUMENT_STATUS_DT,
  RECEIPT_DT,
  SCAN_DT,
  RELEASE_DT,
  JEOPARDY_FLAG,
  0 CREATION_COUNT,
  INVENTORY_COUNT,
  COMPLETION_COUNT
from
  BPM_D_DATES bdd,
  F_NYHIX_MFD_BY_DATE
where
  bdd.D_DATE = BUCKET_START_DATE
  and CREATION_COUNT = 1
)
union all
-- Completion day when not completed on the first day.
select
  FNMFDBD_ID,
  bdd.D_DATE,
  NYHIX_MFD_BI_ID,
  DNMFDDT_ID,
  DNMFDDS_ID,
  DNMFDES_ID,
  DNMFDDST_ID,
  DNMFDFT_ID,
  DNMFDTS_ID,
  DNMFDIS_ID,
  INSTANCE_STATUS_DT,
  DOCUMENT_STATUS_DT,
  RECEIPT_DT,
  SCAN_DT,
  RELEASE_DT,
  JEOPARDY_FLAG,
  CREATION_COUNT,
  INVENTORY_COUNT,
  COMPLETION_COUNT
from
  BPM_D_DATES bdd,
  F_NYHIX_MFD_BY_DATE
where
  bdd.D_DATE >= BUCKET_START_DATE
  and bdd.D_DATE = BUCKET_END_DATE
  and CREATION_COUNT = 0
  and COMPLETION_COUNT = 1
with read only;
