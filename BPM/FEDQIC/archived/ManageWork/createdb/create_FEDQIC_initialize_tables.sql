-- Create  Common tables for run initialization 


CREATE TABLE FEDQIC_MAXDAT_STG (
   ID                      NUMBER
 , C_ASSIGNMENT_FROM       NUMBER
 , C_ASSIGNMENT_TO         NUMBER
 , C_CREATED_BY            NUMBER
 , C_CREATED_ON            DATE
 , ASSIGNMENT_ID           NUMBER
 , DATA_OBJECT_KEY         VARCHAR2(10)
 , TRACKING_ID             NUMBER
 , SUBJECT_TYPE            NUMBER
 , SUBJECT_ID              NUMBER
 , ROLE_ID                 NUMBER
 , IS_CURRENT              NUMBER
 , IS_DELEGATE             NUMBER
 , IS_CREATOR              NUMBER
 , ASSIGNMENT_DATE         DATE
 , DESCRIPTION             VARCHAR2(200)
 , STG_EXTRACT_DATE        DATE          DEFAULT SYSDATE
 , STG_LAST_UPDATE_DATE    DATE          DEFAULT SYSDATE
 , PREVIOUS_ASSIGNMENT_ID  NUMBER
 , C_ASSIGNED_DATE         DATE
 , C_RECEIVED_DATE         DATE
 , C_CASE_NUMBER           VARCHAR2(100)
 , C_CLAIM_TYPE0           NUMBER
 , MW_PROCESSED            VARCHAR2(1)   DEFAULT 'N'
)
tablespace MAXDAT_DATA;

  alter table FEDQIC_MAXDAT_STG add constraint CMAX_STG_PK primary key (ID)
  using index tablespace MAXDAT_INDX;


Grant select on FEDQIC_MAXDAT_STG to MAXDAT_READ_ONLY;


