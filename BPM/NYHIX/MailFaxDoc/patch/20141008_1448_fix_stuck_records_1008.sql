DECLARE
  CURSOR s1
  IS
    SELECT
      q.identifier
    FROM
      BPM_UPDATE_EVENT_QUEUE q
    INNER JOIN NYHIX_ETL_MAIL_FAX_DOC s
    ON
      (q.identifier = s.dcn and s.doc_status_dt is not null)
    WHERE
      BSL_ID = 18
    AND IDENTIFIER LIKE 'U%';
BEGIN
  FOR c1 IN s1
  LOOP
    UPDATE
      BPM_UPDATE_EVENT_QUEUE
    SET
      OLD_DATA = deletexml(OLD_DATA,'/ROWSET/ROW/DOC_STATUS_DT'),
      NEW_DATA = deletexml(NEW_DATA,'/ROWSET/ROW/DOC_STATUS_DT')
    WHERE
      BSL_ID                                            = 18
    AND IDENTIFIER                                      =c1.IDENTIFIER
    AND existsnode(old_data,'ROWSET/ROW/DOC_STATUS_DT') = 1;
    
   COMMIT;
  END LOOP;
END;
