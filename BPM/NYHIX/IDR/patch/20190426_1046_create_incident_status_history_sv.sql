CREATE OR REPLACE VIEW MAXDAT.INCIDENT_STATUS_HISTORY_SV
AS
SELECT 
      INCIDENT_HEADER_STAT_HIST_ID
    , INCIDENT_HEADER_ID   INCIDENT_ID   
    , STATUS_CD      STATUS
    , CREATED_BY               
    , CREATE_TS           
    , CREATED_BY_STAFF_ID           
    , INCIDENT_STATUS
FROM MAXDAT.INCIDENT_HEADER_STAT_HIST_STG
WITH READ ONLY;

GRANT SELECT ON INCIDENT_STATUS_HISTORY_SV TO MAXDAT_READ_ONLY;
GRANT SELECT ON INCIDENT_STATUS_HISTORY_SV TO DP_SCORECARD;