CREATE OR REPLACE FORCE VIEW MAXDAT.F_MFB_BY_BATCH_GROUP_BY_DAY_SV
(
    D_DATE,
    WEEKEND_FLAG,
    BATCH_GROUP,
    BATCH_CLASS,
    BATCH_PROGRAM,
    FAX_BATCH_SOURCE,
    CREATION_COUNT,
    COMPLETION_COUNT,
    CANCEL_COUNT,
    REPROCESSED_COUNT,
    INVENTORY_COUNT
)
BEQUEATH DEFINER
AS
      SELECT D_DATE,
             WEEKEND_FLAG,
             Batch_Group,
             BATCH_CLASS,
             BATCH_PROGRAM,
             FAX_BATCH_SOURCE,
             SUM (CREATION_COUNT)        AS CREATION_COUNT,
             SUM (COMPLETION_COUNT)      AS COMPLETION_COUNT,
             SUM (CANCELATION_COUNT)     AS CANCEL_COUNT,
             SUM (REPROCESSED_COUNT)     AS REPROCESSED_COUNT,
             SUM (INVENTORY_COUNT)       AS INVENTORY_COUNT
        FROM maxdat.F_MFB_V2_BY_DAY
    GROUP BY D_DATE,
             WEEKEND_FLAG,
             Batch_Group,
             BATCH_CLASS,
             BATCH_PROGRAM,
             FAX_BATCH_SOURCE;


