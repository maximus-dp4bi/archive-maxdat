CREATE OR REPLACE FORCE VIEW MAXDAT.D_MFBBATCH_EVENTS_CURMOD_SV
(
    CURRENT_BATCH_MODULE_ID,
    BATCH_GUID,
    MODULE_LAUNCH_ID,
    MODULE_UNIQUE_ID,
    MODULE_NAME,
    MODULE_CLOSE_UNIQUE_ID,
    MODULE_CLOSE_NAME,
    BATCH_STATUS,
    MODULE_START_DT,
    MODULE_END_DT,
    MODULE_LAUNCH_USER_NAME,
