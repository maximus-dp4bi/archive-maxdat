CREATE OR REPLACE FORCE VIEW MAXDAT.F_MFB_V2_BY_HOUR_SV
(
    FMFBBH_ID,
    D_DATE_HOUR,
    MFB_BI_ID,
    CREATION_COUNT,
    INVENTORY_COUNT,
    COMPLETION_COUNT
)
