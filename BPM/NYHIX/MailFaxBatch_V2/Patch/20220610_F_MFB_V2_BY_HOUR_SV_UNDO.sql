AS
    SELECT MFB_V2_BI_ID     AS FMFBBH_ID,
           D_DATE           AS date_hour,
           MFB_V2_BI_ID,
           CREATION_COUNT,
           INVENTORY_COUNT,
           COMPLETION_COUNT
      FROM F_MFB_V2_BY_DAY
