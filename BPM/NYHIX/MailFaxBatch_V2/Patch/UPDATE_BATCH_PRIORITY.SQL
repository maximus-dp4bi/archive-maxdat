UPDATE MAXDAT.NYHIX_MFB_V2_BATCH_SUMMARY X
SET BATCH_PRIORITY =
(
SELECT BATCH_PRIORITY
FROM MAXDAT.CORP_ETL_MFB_BATCH Y
WHERE Y.SOURCE_SERVER = X.SOURCE_SERVER
AND Y.BATCH_GUID = X.BATCH_GUID
)
WHERE (X.SOURCE_SERVER, X.BATCH_GUID )
IN (
SELECT V2.SOURCE_SERVER, V2.BATCH_GUID
FROM MAXDAT.NYHIX_MFB_V2_BATCH_SUMMARY V2
JOIN MAXDAT.CORP_ETL_MFB_BATCH V1
ON V1.SOURCE_SERVER = V2.SOURCE_SERVER
AND V1.BATCH_GUID = V2.BATCH_GUID
JOIN MAXDAT.NYHIX_MFB_V2_STATS_BATCH SB
ON SB.SOURCE_SERVER = V2.SOURCE_SERVER
AND SB.BATCH_GUID = V2.BATCH_GUID
WHERE NVL(V2.BATCH_PRIORITY,-1) <> NVL(V1.BATCH_PRIORITY,-1)
and to_char(v2.create_dt,'YYYYMM') = '202202'
);
COMMIT;
