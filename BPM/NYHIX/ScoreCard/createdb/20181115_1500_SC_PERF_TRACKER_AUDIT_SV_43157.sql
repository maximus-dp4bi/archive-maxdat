CREATE OR REPLACE VIEW DP_SCORECARD.SC_PERF_TRACKER_AUDIT_SV AS
SELECT
    RECORD_TYPE
    , RECORD_ACTION
    , PT_ID
    , STAFF_ID
    , PT_ENTRY_DATE
    , DL_ID
    , COMMENTS
    , CREATE_BY
    , CREATE_DATETIME
    , LAST_UPDATED_BY
    , LAST_UPDATED_DATETIME
    , TRANSACTION_DATE
FROM DP_SCORECARD.SC_PERFORMANCE_TRACKER_AUDIT
WITH read only;
