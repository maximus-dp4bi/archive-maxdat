CREATE OR REPLACE VIEW DP_SCORECARD.SC_ATTENDANCE_AUDIT_SV 
as
SELECT
    RECORD_TYPE
    , RECORD_ACTION
    , SC_ATTENDANCE_ID
    , STAFF_ID
    , ENTRY_DATE
    , SC_ALL_ID
    , ABSENCE_TYPE
    , POINT_VALUE
    , CREATE_BY
    , CREATE_DATETIME
    , BALANCE
    , INCENTIVE_BALANCE
    , TOTAL_BALANCE
    , INCENTIVE_FLAG
    , LAST_UPDATED_BY
    , LAST_UPDATED_DATETIME
FROM DP_SCORECARD.SC_ATTENDANCE_AUDIT
WITH read only;

