--create view PP_WFM_ACTUALS_SV

  CREATE OR REPLACE FORCE VIEW "MAXDAT"."PP_WFM_ACTUALS_SV" 
  (RT_ACTUALS_ID
  , STAFF_ID
  , EVENT_ID
  , SOURCE_ID
  , TASK_START
  , TASK_CATEGORY_ID
  , TASK_END
  , WORK_ACTIVITY
  , ANNOTATION
  , CREATE_DATE
  , CREATE_STAFF_ID
  , MODIFY_DATE
  , MODIFY_STAFF_ID
  , QUEUE_ID
  , EXCLUSION_FLAG
  , WORK_SUBACTIVITY
  , HANDLE_TIME
  ) AS 
  select 
RT_ACTUALS_ID
,STAFF_ID
,EVENT_ID
,SOURCE_ID
,TASK_START
,TASK_CATEGORY_ID
,TASK_END
,WORK_ACTIVITY
,ANNOTATION
,CREATE_DATE
,CREATE_STAFF_ID
,MODIFY_DATE
,MODIFY_STAFF_ID
,QUEUE_ID
,EXCLUSION_FLAG
,WORK_SUBACTIVITY
,round(((task_end-task_start) * 24 * 60),2) as HANDLE_TIME
from PP_WFM_ACTUALS
WITH READ ONLY;

grant select on PP_WFM_ACTUALS_SV to MAXDAT_READ_ONLY; 
grant select on PP_WFM_ACTUALS_SV to DP_SCORECARD; 
grant select on PP_WFM_ACTUALS to MAXDAT_MSTR_TRX_RPT;