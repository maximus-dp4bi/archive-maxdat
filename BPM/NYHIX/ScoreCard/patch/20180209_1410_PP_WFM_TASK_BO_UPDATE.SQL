UPDATE DP_SCORECARD.PP_WFM_TASK_BO BO
SET ( DELETE_FLAG, DELETE_DETECTED_DATE)
= ( SELECT 'Y', SYSDATE
	FROM DP_SCORECARD.PP_WFM_TASK_BO_DELETES DELS
	WHERE BO.TASK_ID = DELS.TASK_ID 
	)
WHERE NVL(BO.DELETE_FLAG,'N') = 'N'
AND BO.TASK_ID IN ( SELECT TASK_ID FROM DP_SCORECARD.PP_WFM_TASK_BO_DELETES )
/	

