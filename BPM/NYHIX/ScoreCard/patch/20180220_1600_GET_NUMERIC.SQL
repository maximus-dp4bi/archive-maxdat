CREATE OR REPLACE FUNCTION DP_SCORECARD.GET_NUMERIC( NUMBER_STRING IN VARCHAR, DEFAULT_VALUE IN NUMBER DEFAULT NULL )
RETURN NUMBER
IS 
    GOOD_NUMBER NUMBER;
BEGIN
   GOOD_NUMBER := TO_NUMBER(NUMBER_STRING);
   RETURN GOOD_NUMBER;
EXCEPTION
    WHEN OTHERS THEN 
    RETURN DEFAULT_VALUE;
END;
/

