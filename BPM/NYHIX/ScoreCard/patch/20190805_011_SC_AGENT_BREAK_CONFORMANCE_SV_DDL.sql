CREATE OR REPLACE FORCE EDITIONABLE VIEW DP_SCORECARD.SC_AGENT_BREAK_CONFORMANCE_SV 
(AS_DATE, AGENT_ID, EVENTS_SCHEDULED, EVENTS_MET) 
AS 
SELECT AS_DATE,AGENT_ID,EVENTS_SCHEDULED,EVENTS_MET 
FROM DP_SCORECARD.SC_AGENT_BREAK_CONFORMANCE;


GRANT SELECT ON DP_SCORECARD.SC_AGENT_BREAK_CONFORMANCE_SV TO MAXDAT;
GRANT SELECT ON DP_SCORECARD.SC_AGENT_BREAK_CONFORMANCE_SV TO MAXDAT_REPORTS;
GRANT SELECT ON DP_SCORECARD.SC_AGENT_BREAK_CONFORMANCE_SV TO MAXDAT_READ_ONLY;
GRANT SELECT ON DP_SCORECARD.SC_AGENT_BREAK_CONFORMANCE_SV TO DP_SCORECARD_READ_ONLY;
