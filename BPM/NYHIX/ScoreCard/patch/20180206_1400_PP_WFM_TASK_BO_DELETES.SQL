CREATE TABLE DP_SCORECARD.PP_WFM_TASK_BO_DELETES
	TABLESPACE MAXDAT_DATA
AS SELECT distinct TASK_ID, DELETE_DETECTED_DATE 
FROM DP_SCORECARD.PP_WFM_TASK_BO
WHERE DELETE_DETECTED_DATE IS NOT NULL
AND NVL(DELETE_FLAG,'N') <> 'N'
/

CREATE UNIQUE INDEX DP_SCORECARD.PP_WFM_TASK_BO_DELETE_INDX
ON DP_SCORECARD.PP_WFM_TASK_BO_DELETES(TASK_ID )
TABLESPACE MAXDAT_INDX
/

grant select on DP_SCORECARD.PP_WFM_TASK_BO_DELETES to maxdat;

grant select on DP_SCORECARD.PP_WFM_TASK_BO_DELETES to maxdat_read_only;

grant select on DP_SCORECARD.PP_WFM_TASK_BO_DELETES to maxdat_reports;

grant select on DP_SCORECARD.PP_WFM_TASK_BO_DELETES to dp_scorecard_read_only;

