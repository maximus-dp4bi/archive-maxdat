declare  c int;
 begin
    select count(*) into c from user_tables where table_name ='D_NYEC_BATCHES';
    if c = 1 then
       execute immediate 'drop table D_NYEC_BATCHES cascade constraints';
    end if;
 end;
 /



CREATE TABLE MAXDAT.D_NYEC_BATCHES
    (
      BATCH_ID         NUMBER (38) NOT NULL
    , SOURCE           VARCHAR2 (15) NOT NULL
    , DATE_CAPTURED    DATE NOT NULL
    , NAME             VARCHAR2 (255)
    , DOCUMENT_COUNT   NUMBER (10)
    , NUMBER_OF_PAGES  NUMBER (10)
    , SCAN_STATION     VARCHAR2 (255)
    , CREATION_DATE    DATE NOT NULL
    , LAST_UPDATE_DATE DATE NOT NULL,
    CONSTRAINT NYEC_BATCH_PK PRIMARY KEY (BATCH_ID)
    )
    TABLESPACE MAXDAT_DATA
    STORAGE (BUFFER_POOL DEFAULT);


COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.BATCH_ID IS 'Synthetic key generated by sequence';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.SOURCE IS 'Source of batch.  Examples include fax, scan, and bulk load.';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.DATE_CAPTURED IS 'Date document was captured into system for processing.';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.NAME IS 'Name of batch';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.DOCUMENT_COUNT IS 'Number of documents in batch';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.NUMBER_OF_PAGES IS 'Number of pages in batch';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.SCAN_STATION IS 'Workstation used to scan the batch';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.CREATION_DATE IS 'Creation date of the record (audit attribute)';
COMMENT ON COLUMN MAXDAT.D_NYEC_BATCHES.LAST_UPDATE_DATE IS 'When was the record last updated (audit attribute)';
CREATE INDEX D_NYEC_BATCHES_DATE_CAP_IDX
    ON MAXDAT.D_NYEC_BATCHES (DATE_CAPTURED)
    TABLESPACE MAXDAT_INDX;



GRANT SELECT ON MAXDAT.D_NYEC_BATCHES TO MAXDAT_READ_ONLY;

