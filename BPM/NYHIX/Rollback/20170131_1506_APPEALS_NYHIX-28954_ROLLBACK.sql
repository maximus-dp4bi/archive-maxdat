CREATE OR REPLACE VIEW MAXDAT.PP_WFM_ACTUALS_SV
AS
select
RT_ACTUALS_ID
,STAFF_ID
,EVENT_ID
,SOURCE_ID
,TASK_START
,TASK_CATEGORY_ID
,TASK_END
,WORK_ACTIVITY
,ANNOTATION
,CREATE_DATE
,CREATE_STAFF_ID
,MODIFY_DATE
,MODIFY_STAFF_ID
,QUEUE_ID
,EXCLUSION_FLAG
,WORK_SUBACTIVITY
,round(((task_end-task_start) * 24 * 60),2) as HANDLE_TIME
from PP_WFM_ACTUALS WITH READ ONLY;

GRANT SELECT ON MAXDAT.PP_WFM_ACTUALS_SV TO MAXDAT_READ_ONLY;
GRANT SELECT ON MAXDAT.PP_WFM_ACTUALS_SV TO DP_SCORECARD;
