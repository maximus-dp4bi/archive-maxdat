CREATE OR REPLACE VIEW NYHIX_ETL_MFD_FTCH_SV 
AS
SELECT
    FORM_TYPE_CHANGE_HISTORY_ID
    , ECN
    , DCN
    , APP_DOC_DATA_ID
    , OLD_DOCUMENT_TYPE
    , NEW_DOCUMENT_TYPE
    , OLD_DOCUMENT_SUB_TYPE
    , NEW_DOCUMENT_SUB_TYPE
    , OLD_KOFAX_DCN
    , NEW_KOFAX_DCN
    , CREATED_TS
    , CREATED_BY
FROM MAXDAT.FORM_TYPE_CHANGE_HISTORY_STG
WITH read only;

GRANT SELECT ON NYHIX_ETL_MFD_FTCH_SV TO MAXDAT_READ_ONLY;
GRANT SELECT ON NYHIX_ETL_MFD_FTCH_SV TO DP_SCORECARD;


