CREATE OR REPLACE VIEW MAXDAT.SURVEY_RESPONSE_HIST_SV
AS select 
SRH."SURVEY_RESPONSE_HIST_ID",
SRH."SURVEY_ID","TEMPLATE_QUESTION_ID",
SRH."SURVEY_TEMPLATE_ANSWER_ID",
SRH."ANSWER_TEXT","COMMENTS",
SRH."ADDITIONAL_TEXT",
SRH."CREATED_BY",
SRH."CREATE_TS",
SRH."CHALLENGE_REASON",
SRH."CHALLENGE_REASON_COMMENT",
SRH."CHALLENGE_OUTCOME",
SRH."CHALLENGE_FINDINGS",
SRH."CHALLENGE_THIS_QUESTION",
SRH."SURVEY_STATUS_HISTORY_ID",
SSH."STATUS_CD" AS SURVEY_STATUS_CD,
SSH."CREATION_DATE" AS SURVEY_STATUS_UPDATE_DT
from SURVEY_RESPONSE_HIST SRH
LEFT JOIN SURVEY_STATUS_HISTORY SSH ON (SRH.SURVEY_STATUS_HISTORY_ID=SSH.SURVEY_STATUS_HISTORY_ID);

GRANT SELECT ON MAXDAT.SURVEY_RESPONSE_HIST_SV TO maxdat_read_only;
GRANT SELECT ON MAXDAT.SURVEY_RESPONSE_HIST_SV TO maxdat_reports;
