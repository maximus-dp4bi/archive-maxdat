drop table VERINT_SESSIONS_MONTH_STG;
CREATE TABLE VERINT_SESSIONS_MONTH_STG
(
  SESSION_SEGMENT VARCHAR2(2)
, SID_KEY NUMBER(32)
, MONTH_OF_YEAR  NUMBER(32)
, SESS_DURATION  NUMBER(32)
, AGENTID VARCHAR2(15)
, SKILL VARCHAR2(32)
, P4_VALUE VARCHAR2(64)
, P1_VALUE VARCHAR2(128)
, P2_VALUE VARCHAR2(64)
, P3_VALUE VARCHAR2(64)
, P5_VALUE VARCHAR2(64)
, P6_VALUE VARCHAR2(64)
, P7_VALUE VARCHAR2(64)
, P8_VALUE VARCHAR2(64)
, P9_VALUE VARCHAR2(64)
, P10_VALUE VARCHAR2(64)
, P11_VALUE VARCHAR2(64)
, SWITCH_CALL_ID VARCHAR2(16)
, VARIABLE2 VARCHAR2(32)
, local_start_time DATE
, local_end_time DATE
, USER_NAME VARCHAR2(100)
, is_screen INTEGER
, call_id number(32)
, contact_key number(32)
, Unit_Num number(32)
)
    tablespace MAXDAT_DATA
    pctfree 10
    initrans 1
    maxtrans 255
    storage
    (
      initial 3M
      next 1M
      minextents 1
      maxextents unlimited
      freelists 3
    )
    nologging
    ;

grant select on VERINT_SESSIONS_MONTH_STG to MAXDAT_READ_ONLY;

create index VERINT_SESSIONS_MONTH_STG_IDX1 on VERINT_SESSIONS_MONTH_STG (AGENTID, SWITCH_CALL_ID,P4_VALUE, P6_VALUE)  tablespace MAXDAT_INDX nologging  ;

create index VERINT_SESSIONS_MONTH_STG_IDX2 on VERINT_SESSIONS_MONTH_STG (SID_KEY, MONTH_OF_YEAR, SESS_DURATION)  tablespace MAXDAT_INDX nologging  ;

create or replace view VERINT_SESSIONS_MONTH_SV as
select * from VERINT_SESSIONS_MONTH_STG
with read only;


grant select on VERINT_SESSIONS_MONTH_SV to MAXDAT_READ_ONLY;
