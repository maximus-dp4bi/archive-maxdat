CREATE TABLE D_CLIENT_OUTREACH_DEFINITION
(OC_ID NUMBER(18) NOT NULL
 ,OUTREACH_CATEGORY VARCHAR2(64)
 ,OUTREACH_CATEGORY_DESC VARCHAR2(100)
 ,OUTREACH_TYPE VARCHAR2(64)
 ,OUTREACH_TYPE_DESC VARCHAR2(100)
 ,OUTREACH_EVENT_TYPE VARCHAR2(32)
 ,OUTREACH_PROCESS VARCHAR2(32)
 ,LMDEF_ID1 NUMBER(18)
 ,LMDEF_ID2  NUMBER(18)
 ,LMDEF_ID3  NUMBER(18)
 ,LETTER_NAME1 VARCHAR2(40)
 ,LETTER_NAME2 VARCHAR2(40)
 ,LETTER_NAME3 VARCHAR2(40)
 ,FREQUENCY  VARCHAR2(40)
 ,COR_MIN_SENT_PER_DAY  NUMBER(18)
 ,COR_MAX_SENT_PER_DAY  NUMBER(18)
 ,COR_MIN_THRESHOLD  NUMBER(18)
 ,COR_MAX_THRESHOLD  NUMBER(18)) TABLESPACE MAXDAT_DATA;

GRANT SELECT ON D_CLIENT_OUTREACH_DEFINITION TO MAXDAT_READ_ONLY;
grant update on D_CLIENT_OUTREACH_DEFINITION to MAXDAT_READ_ONLY;

alter table D_CLIENT_OUTREACH_DEFINITION add constraint D_CO_DEFINITION_PK primary key (OC_ID) using index tablespace MAXDAT_INDX;

create or replace view D_CLNT_OUTREACH_DEFINITION_SV as
select * 
from D_CLIENT_OUTREACH_DEFINITION
with read only;


grant select on D_CLNT_OUTREACH_DEFINITION_SV to MAXDAT_READ_ONLY;
