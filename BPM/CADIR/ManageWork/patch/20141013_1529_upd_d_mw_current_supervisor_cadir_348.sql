                     from cadir_person_stg p_emp 
                          join d_supervisor s on p_emp.PERSON_ID  = s.person_id
                          join cadir_person_stg p_sup on s.supervisor_person_id = p_sup.PERSON_ID
                          join  cadir_user_stg u on p_emp.PERSON_ID  = u.person_id
                          order by s.last_update_ts,1)
  loop
     update d_mw_current c
        set "Current Team Supervisor Name" = tmp_cur.Super
      where "Current Owner Name" = tmp_cur.owner
        and "Complete Flag" = 'Y';
