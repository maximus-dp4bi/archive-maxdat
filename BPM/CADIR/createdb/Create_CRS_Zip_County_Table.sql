CREATE TABLE D_ZIP_COUNTY
  (
    DZC_ID    	   		        NUMBER (19) NOT NULL ,
    COUNTY_ZIP_CODE			VARCHAR2(255) NOT NULL,    
    CLAIM_ADMIN_COUNTY			VARCHAR2(255),
    CREATE_DT                           DATE NOT NULL ,
    UPDATE_DT                           DATE NOT NULL
  );

ALTER TABLE D_ZIP_COUNTY ADD CONSTRAINT DZC_ID_PK PRIMARY KEY
(
  DZC_ID
)
;

ALTER TABLE D_ZIP_COUNTY ADD CONSTRAINT DZCZ__UN UNIQUE
(
  COUNTY_ZIP_CODE
)
;

CREATE SEQUENCE SEQ_DZC_ID START WITH 1 INCREMENT BY 1 MAXVALUE 9999999999999999999 MINVALUE 1 CACHE 20 ;
 	

CREATE OR REPLACE TRIGGER BI_D_ZIP_COUNTY
    BEFORE INSERT ON D_ZIP_COUNTY
    FOR EACH ROW 
    ENABLE 
BEGIN
IF INSERTING AND :NEW.DZC_ID IS NULL THEN 
          SELECT SEQ_DZC_ID.NEXTVAL INTO :NEW.DZC_ID FROM DUAL;      
END IF;
IF INSERTING THEN
  :NEW.CREATE_DT := SYSDATE;
END IF;
:NEW.UPDATE_DT := SYSDATE;
END;  
/


CREATE OR REPLACE VIEW D_ZIP_COUNTY_SV AS
SELECT
*   
FROM D_ZIP_COUNTY;