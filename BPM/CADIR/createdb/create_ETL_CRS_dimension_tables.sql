--Create 5 dimension tables for CADIR CRS Phase 1

CREATE TABLE D_CRS_CLOSED_REASONS
  (
    DCCR_ID    	   		   NUMBER (19) NOT NULL ,
    CLOSED_REASON_ID               NUMBER (19) NOT NULL ,
    CLOSED_REASON_CODE             VARCHAR2(255) ,
    CLOSED_REASON_NAME             VARCHAR2(255),
    CLOSED_REASON_EFF_DT           DATE,
    CLOSED_REASON_END_DT           DATE,
    CLOSED_REASON_ACTIVE_FLAG      NUMBER (1),
    CREATE_DT                      DATE NOT NULL ,
    UPDATE_DT                      DATE NOT NULL
  );

ALTER TABLE D_CRS_CLOSED_REASONS ADD CONSTRAINT DCCR_ID_PK PRIMARY KEY
(
  DCCR_ID
)
;

ALTER TABLE D_CRS_CLOSED_REASONS ADD CONSTRAINT DCCR__UN UNIQUE
(
  CLOSED_REASON_ID
)
;

