CREATE OR REPLACE TRIGGER BIU_S_CRS_IMR_AUDIT_STAGE
    BEFORE INSERT OR UPDATE ON S_CRS_IMR_AUDIT_STAGE
    FOR EACH ROW 
    ENABLE 
BEGIN
IF INSERTING AND :NEW.SCIAS_ID IS NULL THEN 
          SELECT SEQ_SCIAS_ID.NEXTVAL INTO :NEW.SCIAS_ID FROM DUAL;
END IF;
IF INSERTING THEN 
          :NEW.CREATE_DT := SYSDATE;
          :NEW.CREATED_BY:= USER;
END IF;
:NEW.LAST_UPDATE_DT := SYSDATE;
:NEW.LAST_UPDATED_BY := USER;
END; 
/
