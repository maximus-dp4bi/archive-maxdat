CREATE OR REPLACE VIEW EMRS_D_MARITAL_STATUS_SV
AS
  SELECT 
    c.VALUE AS MARITAL_STATUS_CD
    ,c.REPORT_LABEL AS MARITAL_STATUS
    ,c.EFFECTIVE_START_DATE AS EFFECTIVE_START_DATE
    ,c.EFFECTIVE_END_DATE AS EFFECTIVE_END_DATE
  FROM &schema_name..ENUM_MARITAL_STATUS c;
  
  GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_MARITAL_STATUS_SV TO MAXDAT_REPORTS;
  
  
