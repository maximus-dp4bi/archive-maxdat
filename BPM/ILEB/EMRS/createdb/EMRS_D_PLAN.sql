    , PLAN_SERVICE_TYPE     VARCHAR2 (20)
    , PLAN_ID_EXT           VARCHAR2 (32),
    CONSTRAINT PLAN_PK PRIMARY KEY (PLAN_ID),
    CONSTRAINT PLAN_PLAN_COMB_UK UNIQUE (SOURCE_RECORD_ID, CSDA, MANAGED_CARE_PROGRAM),
    CONSTRAINT PLAN_TYPE_FK FOREIGN KEY (PLAN_TYPE_ID) REFERENCES MAXDAT.EMRS_D_PLAN_TYPE (PLAN_TYPE_ID)
    )
    TABLESPACE MAXDAT_DATA
    STORAGE (BUFFER_POOL DEFAULT);
/

