CREATE TABLE MAXDAT.EMRS_D_PLAN
    (
      PLAN_ID               INTEGER NOT NULL
    , MANAGED_CARE_PROGRAM  VARCHAR2 (50) NOT NULL
    , CSDA                  VARCHAR2 (32)
    , PLAN_CODE             VARCHAR2 (32) NOT NULL
    , PLAN_NAME             VARCHAR2 (64) NOT NULL
    , PLAN_ABBREVIATION     VARCHAR2 (64)
    , PLAN_EFFECTIVE_DATE   DATE
    , ADDRESS1              VARCHAR2 (55)
    , ADDRESS2              VARCHAR2 (55)
    , CITY                  VARCHAR2 (20)
    , STATE                 VARCHAR2 (2)
    , ZIP                   VARCHAR2 (9)
    , ACTIVE                VARCHAR2 (1)
    , CONTACT_FIRST_NAME    VARCHAR2 (30)
    , CONTACT_INITIAL       VARCHAR2 (1)
    , CONTACT_LAST_NAME     VARCHAR2 (30)
    , CONTACT_FULL_NAME     VARCHAR2 (80)
    , CONTACT_PHONE         VARCHAR2 (12)
    , CONTACT_EXTENSION     VARCHAR2 (6)
    , MEMBER_CONTACT_PHONE  VARCHAR2 (12)
    , ENROLLOK              VARCHAR2 (1)
    , PLAN_ASSIGN           VARCHAR2 (1)
    , RECORD_DATE           DATE
    , RECORD_TIME           VARCHAR2 (5)
    , RECORD_NAME           VARCHAR2 (80)
