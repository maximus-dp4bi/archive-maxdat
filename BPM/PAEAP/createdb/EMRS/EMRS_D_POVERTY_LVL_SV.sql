CREATE OR REPLACE VIEW EMRS_D_FEDERAL_POVERTY_LVL_SV
AS
  SELECT NULL AS SOURCE_RECORD_ID ,   
    NULL AS FPL_YEAR ,
    NULL AS FPL_DESCRIPTION ,
    NULL AS FPL_NUMBER_IN_FAMILY ,
    NULL AS FPL_MAX_DOLLARS ,
    NULL AS FPL_LOCALE
  FROM dual;
  
  GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_FEDERAL_POVERTY_LVL_SV TO EB_MAXDAT_REPORTS;