CREATE OR REPLACE VIEW EMRS_D_COVERAGE_CATEGORY_SV
AS
  SELECT 
    NULL AS MANAGED_CARE_PROGRAM ,
    NULL AS SOURCE_RECORD_ID ,
    '0' AS COVERAGE_CATEGORY_CODE ,
    NULL AS COVERAGE_CATEGORY ,
    NULL AS COVERAGE_CATEGORY_TYPE
  FROM dual;
  
  GRANT SELECT ON MAXDAT_SUPPORT.EMRS_D_COVERAGE_CATEGORY_SV TO EB_MAXDAT_REPORTS;