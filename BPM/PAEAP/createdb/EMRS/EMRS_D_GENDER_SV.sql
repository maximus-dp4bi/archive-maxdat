  DESCRIPTION ,
  REPORT_LABEL,
  SCOPE,
  CREATED_BY,
  CREATE_TS,
  UPDATED_BY,
  UPDATE_TS,
  ORDER_BY_DEFAULT,
  EFFECTIVE_START_DATE,
  EFFECTIVE_END_DATE
FROM EB.ENUM_GENDER ;
  
