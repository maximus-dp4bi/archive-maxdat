CREATE OR REPLACE VIEW D_PI_ENROLLMENT_STATUS_SV
AS
  SELECT 'Enrollee' ENROLLMENT_STATUS FROM dual
  UNION ALL
  SELECT 'Potential Enrollee' FROM dual
  UNION ALL
  SELECT 'Other' FROM dual
  UNION ALL
  SELECT 'Enrollee' FROM dual ;
  
  GRANT SELECT ON MAXDAT_SUPPORT.D_PI_ENROLLMENT_STATUS_SV TO EB_MAXDAT_REPORTS ; 