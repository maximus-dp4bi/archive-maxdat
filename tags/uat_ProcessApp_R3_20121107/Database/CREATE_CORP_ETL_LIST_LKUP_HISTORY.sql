CREATE TABLE "MAXDAT"."CORP_ETL_LIST_LKUP_HIST" 
   ("CELL_HISTORY_ID" NUMBER NOT NULL ENABLE,
    "HIST_TYPE" VARCHAR2(100),
     "CELL_ID" NUMBER NOT NULL ENABLE, 
  "NAME" VARCHAR2(30) NOT NULL ENABLE, 
  "LIST_TYPE" VARCHAR2(30) NOT NULL ENABLE, 
  "VALUE" VARCHAR2(100) NOT NULL ENABLE, 
  "OUT_VAR" VARCHAR2(100), 
  "REF_TYPE" VARCHAR2(100), 
  "REF_ID" NUMBER(38,0), 
  "START_DATE" DATE, 
  "END_DATE" DATE, 
  "COMMENTS" VARCHAR2(4000), 
  "CREATED_TS" DATE NOT NULL ENABLE, 
  "UPDATED_TS" DATE NOT NULL ENABLE,
  "HIST_CREATED_TS" DATE NOT NULL ENABLE,
  "HIST_USER" VARCHAR2(4000),
  "HIST_UPDATED_TS" DATE NOT NULL ENABLE,
 CONSTRAINT "CORP_ETL_LIST_LKUP_HIST_PK" PRIMARY KEY ("CELL_HISTORY_ID")  
);

CREATE SEQUENCE  "MAXDAT"."CELL_HISTORY_SEQ"  MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE ;


   insert into CORP_ETL_LIST_LKUP_HIST 
          (select 
          CELL_HISTORY_SEQ.nextval,--"CELL_HISTORY_ID" NUMBER NOT NULL ENABLE,
           'ORIG',--"HIST_TYPE" VARCHAR2(100),
           cell_id,--"CELL_ID" NUMBER NOT NULL ENABLE, 
           name,--"NAME" VARCHAR2(30) NOT NULL ENABLE, 
           LIST_TYPE,--"LIST_TYPE" VARCHAR2(30) NOT NULL ENABLE, 
           VALUE,--"VALUE" VARCHAR2(100) NOT NULL ENABLE, 
           OUT_VAR,--"OUT_VAR" VARCHAR2(100), 
           REF_TYPE,--"REF_TYPE" VARCHAR2(100), 
           REF_ID,--"REF_ID" NUMBER(38,0), 
           START_DATE,--"START_DATE" DATE, 
           END_DATE,--"END_DATE" DATE, 
           COMMENTS,--"COMMENTS" VARCHAR2(4000), 
           CREATED_TS,--"CREATED_TS" DATE NOT NULL ENABLE, 
           UPDATED_TS,--"UPDATED_TS" DATE NOT NULL ENABLE,
           sysdate,--"HIST_CREATED_TS" DATE NOT NULL ENABLE, 
           user,   --HIST_USER
           sysdate--"HIST_UPDATED_TS" DATE NOT NULL ENABLE,
           from corp_etl_list_lkup
           );
