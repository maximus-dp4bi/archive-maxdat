CREATE TABLE IL_COHORTS_STG
 (
   CLIENT_ID          NUMBER (10)
 , MAILING_COHORT     NUMBER (8)
 , MAILING_REQUEST_TS DATE
 , PASSIVE_COHORT     NUMBER (8)
 , PASSIVE_RELEASE_TS DATE
 )
 TABLESPACE MAXDAT_DATA;

CREATE INDEX COHORT_IDX_1 ON IL_COHORTS_STG (CLIENT_ID) TABLESPACE MAXDAT_INDX;

CREATE INDEX COHORT_IDX_2 ON IL_COHORTS_STG (PASSIVE_COHORT) TABLESPACE MAXDAT_INDX;

CREATE OR REPLACE PUBLIC SYNONYM IL_COHORTS_STG FOR IL_COHORTS_STG; 
GRANT SELECT ON "IL_COHORTS_STG" TO "MAXDAT_READ_ONLY";      