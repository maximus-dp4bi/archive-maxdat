
UPDATE CC_S_CONTACT_QUEUE ocq
SET UNIT_OF_WORK_ID = (SELECT uow.UNIT_OF_WORK_ID 
                        FROM CC_C_UNIT_OF_WORK uow 
                        INNER JOIN CC_C_CONTACT_QUEUE ccq on ccq.UNIT_OF_WORK_NAME = uow.UNIT_OF_WORK_NAME
                        WHERE ccq.QUEUE_NUMBER = ocq.QUEUE_NUMBER)
WHERE QUEUE_NUMBER NOT IN (0);

