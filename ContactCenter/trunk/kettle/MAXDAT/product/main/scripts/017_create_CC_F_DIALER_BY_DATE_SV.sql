-- Semantic View

CREATE OR REPLACE VIEW CC_F_DIALER_DETAIL_SV as
SELECT 
 F.F_DIALER_ID
,F.DATETIME
,F.D_DATE_ID
,F.CALL_BACK_DATETIME
,F.FIRST_NAME
,F.LAST_NAME
,F.D_AGENT_ID
,F.AGENT_LOGIN_ID
,F.ROUTER_CALL_KEY_DAY
,F.ROUTER_CALL_KEY
,F.PERIPHERAL_CALL_KEY
,F.PERIPHERAL_ID
,F.SKILL_GROUP_ID
,F.CALLGUID
,F.CAMPAIGN_ID
,F.CAMPAIGN_NAME
,F.PHONE
,F.CUSTOMER_ACCOUNT_NUMBER
,F.CALL_RESULT
,F.DIALING_MODE
,F.CALL_DURATION
,F.CALL_HANDLE_METHOD
,F.CALL_DIAL_METHOD
,F.DIALING_LIST_ID
,F.EXTRACT_DATE
,F.LAST_UPDATE_DATE
,F.LAST_UPDATED_BY
,(SELECT PROJECT_ID FROM CC_D_PROJECT WHERE PROJECT_NAME = C.PROJECT_NAME) D_PROJECT_ID
,(SELECT PROGRAM_ID FROM CC_D_PROGRAM WHERE PROGRAM_NAME = C.PROGRAM_NAME) D_PROGRAM_ID
FROM CC_F_DIALER_BY_DATE F
LEFT OUTER JOIN CC_C_CAMPAIGN_SV C ON F.CAMPAIGN_ID = C.CAMPAIGN_ID;
  
GRANT SELECT ON CC_F_DIALER_BY_DATE_SV TO MAXDAT_READ_ONLY; 