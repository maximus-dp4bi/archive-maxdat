-- Staging table

CREATE TABLE CC_S_V2_CALL
  (
    S_V2_CALL_ID  		NUMBER (19) NOT NULL ,
    D_DATE                      DATE NOT NULL,
    DATETIME                    DATE NOT NULL ,
    CALLTYPE_REPORTING_DATETIME                   DATE,
    QUEUE_NUMBER                NUMBER (19),
    S_CONTACT_QUEUE_ID          NUMBER (19)  , 
    AGENT_SKILL_TARGET_ID       NUMBER (19),
    AGENT_LOGIN_ID		VARCHAR2(50),
    S_AGENT_ID			NUMBER(19) ,
    ROUTER_CALL_KEY_DAY         NUMBER(19),
    ROUTER_CALL_KEY             NUMBER(19),
    PERIPHERAL_CALL_KEY         NUMBER(19),
    PERIPHERAL_CALL_TYPE        NUMBER(19),
    PERIPHERAL_ID               NUMBER(19) NOT NULL,
    SKILL_GROUP_ID              NUMBER(19),
    PRECISION_QUEUE_ID          NUMBER(19),
    SOURCE_AGENT_SKILL_TARGET_ID NUMBER(19),
    SOURCE_AGENT_LOGIN_ID       VARCHAR2(50),
    SOURCE_S_AGENT_ID           NUMBER(19),
    CALL_REFERENCE_ID		VARCHAR2(50),
    CALLGUID                    VARCHAR2(50),
    CALL_SEGMENT_ID		VARCHAR2(50), -- no mapping
    SOURCE_CALL_ID		VARCHAR2(40), -- no mapping
    ACTIVITY_ID			VARCHAR2(50), -- no mapping
    DIGITS_DIALED               VARCHAR2(50),
    ANI                         VARCHAR2(50), -- ANI_PHONE_NUMBER
    CALL_DISPOSITION            NUMBER(19) NOT NULL,
    CALL_DISPOSITION_FLAG       NUMBER(19),
    DNIS			VARCHAR2(30),
    LANGUAGE			VARCHAR2(50),
    NETWORK_SKILL_GROUP_Q_TIME  NUMBER(11,2), -- QUEUE_TIME_SECONDS
    RING_TIME  		        NUMBER(11,2),
    HOLD_TIME    		NUMBER(11,2),
    WORK_TIME	                NUMBER(11,2),
    TALK_TIME    		NUMBER(11,2),
    NET_Q_TIME   		NUMBER(11,2), -- IVR_TIME_SECONDS
    NETWORK_TIME                NUMBER(11,2),
    LOCAL_Q_TIME                NUMBER(11,2),
    DELAY_TIME                  NUMBER(11,2),
    TIME_TO_ABAND               NUMBER(11,2),
    CALL_DURATION		NUMBER(19),
    TRANSFER_TO			VARCHAR2(200),
    XFERRED_OUT_FLAG		VARCHAR2(1),
    VOICEMAIL_FLAG		VARCHAR2(1),
    CALL_ABANDONED_FLAG         NUMBER(1),
    CALL_OFFERED_FLAG		NUMBER(1),
    CALL_ANSWERED_FLAG		NUMBER(1),
    CALL_BUSY_FLAG              NUMBER(1),
    CALL_DROPPED_FLAG           NUMBER(1),
    CALL_HANDLE_METHOD          VARCHAR2(50),
    CALL_DIAL_METHOD            VARCHAR2(50),
    CALL_SEGMENT_END_DT		DATE, -- no mapping
    EXTRACT_DATE                DATE DEFAULT SYSDATE NOT NULL,
    LAST_UPDATE_DATE            DATE DEFAULT SYSDATE NOT NULL ,
    LAST_UPDATED_BY 		VARCHAR2(30) NOT NULL
  ) ;
  
  CREATE INDEX CC_S_V2_CALL_IDX1 ON CC_S_V2_CALL
      (
        S_CONTACT_QUEUE_ID ASC
      ) ;
      
  CREATE INDEX CC_S_V2_CALL_IDX2 ON CC_S_V2_CALL
            (
              S_AGENT_ID ASC
      ) ;
    
  CREATE INDEX CC_S_V2_CALL_IDX3 ON CC_S_V2_CALL
          (
            QUEUE_NUMBER ASC
      ) ;
      
  CREATE INDEX CC_S_V2_CALL_IDX4 ON CC_S_V2_CALL
          (
            AGENT_LOGIN_ID ASC
      ) ;  
      
  CREATE INDEX CC_S_V2_CALL_IDX5 ON CC_S_V2_CALL
          (
            ROUTER_CALL_KEY_DAY ASC
      ) ;   
      
  CREATE INDEX CC_S_V2_CALL_IDX6 ON CC_S_V2_CALL
          (
            ROUTER_CALL_KEY ASC
      ) ;  
  CREATE INDEX CC_S_V2_CALL_IDX7 ON CC_S_V2_CALL
          (
            D_DATE ASC
      ) ;     
    
    ALTER TABLE CC_S_V2_CALL ADD CONSTRAINT CC_S_V2_CALL_PK PRIMARY KEY
    (
      S_V2_CALL_ID
    )
    ;
    
    ALTER TABLE CC_S_V2_CALL ADD CONSTRAINT S_V2_CALL_S_CNTCT_Q_FK FOREIGN KEY ( S_CONTACT_QUEUE_ID ) REFERENCES CC_S_CONTACT_QUEUE ( CONTACT_QUEUE_ID ) NOT DEFERRABLE ;
    
    ALTER TABLE CC_S_V2_CALL ADD CONSTRAINT S_V2_CALL_S_AGENT_FK FOREIGN KEY ( S_AGENT_ID ) REFERENCES CC_S_AGENT ( AGENT_ID ) NOT DEFERRABLE ;
       

    
  CREATE SEQUENCE SEQ_CC_S_V2_CALL START WITH 1 INCREMENT BY 1 MAXVALUE 9999999999999999999 MINVALUE 1 CACHE 20;
  
  CREATE OR REPLACE TRIGGER BIU_CC_S_V2_CALL
      BEFORE INSERT OR UPDATE ON CC_S_V2_CALL
      FOR EACH ROW 
  BEGIN 
  IF INSERTING AND :NEW.S_V2_CALL_ID IS NULL THEN 
            SELECT SEQ_CC_S_V2_CALL.NEXTVAL INTO :NEW.S_V2_CALL_ID FROM DUAL;
  END IF;
  IF INSERTING THEN 
            :NEW.EXTRACT_DATE := SYSDATE;
  END IF;
  :NEW.LAST_UPDATE_DATE := SYSDATE;
  :NEW.LAST_UPDATED_BY := USER;
  END; 
