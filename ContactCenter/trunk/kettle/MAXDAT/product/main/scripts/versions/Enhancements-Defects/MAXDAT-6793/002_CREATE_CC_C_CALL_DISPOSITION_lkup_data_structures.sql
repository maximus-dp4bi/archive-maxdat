CREATE TABLE CC_C_CALL_DISPOSITION_LKUP
  (
    CALL_DISPOSITION_CODE       NUMBER (19) NOT NULL,
    CALL_DISPOSITION_DESC       VARCHAR2(100) NOT NULL,
    CREATE_DATE                 DATE DEFAULT SYSDATE NOT NULL ,
    LAST_UPDATE_DATE            DATE DEFAULT SYSDATE NOT NULL ,
    LAST_UPDATED_BY 		VARCHAR2(30)
  ) ;
  
  ALTER TABLE CC_C_CALL_DISPOSITION_LKUP ADD CONSTRAINT C_CALL_DISPOSITION_LKUP_PK PRIMARY KEY
    (
      CALL_DISPOSITION_CODE
    )
    ;
    
  CREATE OR REPLACE TRIGGER BIU_C_CALL_DISPOSITION_LKUP
      BEFORE INSERT OR UPDATE ON CC_C_CALL_DISPOSITION_LKUP
      FOR EACH ROW 
  BEGIN 
  IF INSERTING THEN 
            :NEW.CREATE_DATE := SYSDATE;
  END IF;
  :NEW.LAST_UPDATE_DATE := SYSDATE;
  :NEW.LAST_UPDATED_BY := USER;
  END; 
  /     
  
  
