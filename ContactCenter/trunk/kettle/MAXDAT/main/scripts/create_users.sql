--create role(s)
CREATE ROLE etluser 
    NOT IDENTIFIED 
;

--assign privileges
GRANT CREATE SESSION, CREATE TABLE, CREATE SEQUENCE, CREATE TRIGGER, CREATE VIEW, CREATE PUBLIC SYNONYM, CREATE TABLESPACE TO etluser
;

--create user(s)

CREATE USER projectuser 
    IDENTIFIED BY M21
	DEFAULT TABLESPACE PROJ_STG
    QUOTA UNLIMITED ON PROJ_STG 
	QUOTA UNLIMITED ON MAXDAT_DATA
	QUOTA UNLIMITED ON MAXDAT_INDX
    QUOTA 1M ON SYSTEM
    ACCOUNT UNLOCK 
;

--assign role(s)
GRANT etluser TO projectuser 
;

