CREATE OR REPLACE TRIGGER BI_CC_D_COUNTRY 
    BEFORE INSERT ON CC_D_COUNTRY 
    FOR EACH ROW 
    ENABLE 
BEGIN
IF INSERTING AND :NEW.COUNTRY_ID IS NULL THEN 
          SELECT SEQ_CC_D_COUNTRY.NEXTVAL INTO :NEW.COUNTRY_ID FROM DUAL;      
END IF;
END;  
